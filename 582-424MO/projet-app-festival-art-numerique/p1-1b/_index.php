<checklist>
   Aller sur un site de palette de couleurs tels que <a href="https://coolors.co/" onclick="event.stopPropagation()" target="_blank">Coolors</a> (voir les ressources dans le wiki du cours pour la liste des outils de palettes de couleurs) Vous devez choisir une palette de 5 couleurs. Votre palette doit contenir au moins une couleur très foncée et une couleur très pâle qui servira de couleur de texte et d'arrière-plan de base. Les autres couleurs vous permettront de d'ajouter des accents de couleur au besoin (boutons, liens, navigation, cadre du pied de page, etc). Pour plus d'info sur les palettes de couleurs sur un site web consulter cet article de <a href="https://99designs.ca/blog/tips/website-color-schemes/" onclick="event.stopPropagation()" target="_blank">99 designs</a> et cet article de <a href="https://www.canva.com/learn/website-color-schemes/" onclick="event.stopPropagation()" target="_blank">canva</a>. 
   Aller sur un site de typo (polices de caractère) afin de choisir 1 à 2 typos pour le texte de votre projet (voir les ressources dans le wiki du cours pour la liste des sites où aller chercher des typos gratuites). Si vous en choisissez 2, ce sera une pour les titres et une très distincte et plus lisible pour les petits caractères pour le contenu textuel courant. Mais l'idéal pour un petit projet c'est de se concentrer sur une seule famille de typo qui comporte plusieurs niveaux de graisse.
   Choisir le logo de l'événement sélectionné ou en redessiner un autre, comme un re-branding. Je laisse ceci entre vos mains, c'est à vous de voir.
   Créer la version  <a href="https://fr.wikipedia.org/wiki/Favicon" target="_blank" rel="noopener noreferrer">favicon</a> de votre logo. Format carré 32px x 32px en fichier <em>PNG</em> avec fond transparent. Assurez vous que le logo ait une belle apprence même si le format de l'image est très petit. Au besoin vous pourrez créer une variation alternative du logo pour améliorer sa visibilité en format favicon.
 </checklist>

