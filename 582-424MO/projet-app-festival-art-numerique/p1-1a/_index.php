 <h3>Contenu de l'appli</h3>
 <p>Pour les contenus, vous devez choisir des œuvres et des artistes réels. Vous pouvez sélectionner des œuvres et des artistes parmis ceux qui sont présentés dans les différents festivals listés ci-dessus, peu importe l'édition. Il n'est pas nécessaire de sélectionner des artistes et oeuvres du festivals choisi, vous pouvez mélanger les contenus et choisir ce qui vous inspire le plus. Il faudra choisir au moins 6 œuvres d'artistes différents.</p>
