 <p>Pour ce projet, en équipe de deux, vous allez créer l'interface d'une application mobile ainsi qu'une page de destination (aussi appelée <em>Landing Page</em>) qui fera la promotion de cette appli.</p>

 <p>À titre de clarté, voici les abbréviations que nous allons utiliser tout au long du projet:</p>
 <ul>
   <li> <strong>App</strong> : Application mobile </li>
   <li> <strong>LP</strong> : Landing Page (page de promotion de l'application mobile)</li>
</ul>

 <p>L'application mobile est destinée à être téléchargée et utilisée par les participants d'un événement d'art numérique.</p>
   

