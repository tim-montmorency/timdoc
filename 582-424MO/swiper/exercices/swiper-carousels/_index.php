<grostitre>Étape 2</grostitre>

<checklist>Donnez la hauteur de 300px à votre carrousel ainsi qu'à toutes les images à l'intérieur de&nbsp;celui-ci.
Les&nbsp;<em>slides</em>&nbsp;du carrousel doivent se déplacer verticalement ↕️&nbsp;plutôt&nbsp;qu'horizontalement.
Le carrousel doit changer de&nbsp;<em>slide</em>&nbsp;automatiquement à toute les 1.5&nbsp;secondes&nbsp;⏱.</checklist>

<p>Aperçu du résultat 👇</p>
<clipasset src="videos/exercice-swiper-etape2.mp4"></clipasset>

<dots></dots>
<grostitre>Étape 3</grostitre>


<checklist>Le carrousel doit tourner à l'infini horizontalement&nbsp;↔️&nbsp;<em>(possibilité de swiper à l'infini dans une&nbsp;direction)</em>.
À partir de 768px, deux&nbsp;<em>slides</em>&nbsp;🏙🌄&nbsp;doivent être visibles en même temps plutôt qu'une&nbsp;seule et un espace de 25px doit-être ajouté entre chacune des&nbsp;<em>slides</em>.
À partir de 992px, trois&nbsp;<em>slides</em>&nbsp;🏙🌄🌉&nbsp;doivent être visibles en même temps plutôt que&nbsp;deux et un espace de 25px doit-être ajouté entre chacune des&nbsp;<em>slides</em>.</checklist>


<p>Aperçu du résultat 👇</p>
