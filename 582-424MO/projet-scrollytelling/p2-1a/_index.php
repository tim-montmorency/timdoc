<h3>Types d’animations obligatoires à prévoir dans votre scénario</h3>
<ol>
   <li>Prévoir la transition entre chaque chapitre, au moins 3 transitions différentes.</li>
   <li>Défiler (scroller) dans un paysage à plusieurs couches avec effet parallax (le fond bouge moins vite que les éléments plus près).</li>
   <li>Morphing entre 2 formes simples (2 éléments svg).</li>
   <li>Un élément qui suit une trajectoire non linéaire (une trajectoire en bezier, par exemple l’élément pourrait suivre un tracé en forme ce cœur ❤️).</li>
   <li>Animation de texte à prévoir (entrée et/ou sortie de titres et/ou paragraphes à l’écran).</li>
   <li>Quelque chose qui se compose ou se décompose en couche ou en éclats via le défilement de la page.</li>
   <li>Forme (ligne) simple qui de dessine à mesure qu’on défile (scroll).</li>
   <li>Au moins un chapitre avec animation automatique en boucle, qui s’active sans qu’on ait besoin de défiler (scroller).</li>
</ol>
