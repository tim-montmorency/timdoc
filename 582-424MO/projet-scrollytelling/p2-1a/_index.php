<grostitre>Scénarimage (scénario)</grostitre>

<p>Utilisez le logiciel <a href="https://wonderunit.com/storyboarder/" target="_blank">Storyboarder</a> pour réaliser votre scénarimage. Voir ma capsule vidéo qui montre comment utiliser ce logiciel gratuit.</p>

<h3>Format d'exportation pour la remise</h3>
<p>Vous devez exporter votre storyboard en PDF: 4 case par page, 2 rangées, 2 colonnes.</p>
<h3>Spécifications</h3>
<p>Histoire entre 6 et 8 chapitres </p>
<p>Concrètement, chaque chapitre représentera une section dans la page, chaque section, une en dessous de l’autre.
Chaque case du scénarimage sera en format 16/9 (format écran standard) et démontrera ce qu’on voit à l’écran pour chaque chapitre.</p>
<p>Chaque chapitre peut comporter plusieurs plans afin qu’on visualise bien les animations à l’intérieur de celle-ci. Donc dans votre scénarimage, il est possible d’avoir plusieurs cases pour illustrer le même chapitre.</p>

<p>Dans la description de chaque case :</p>
<ul>
   <li>Numéro du chapitre (Chapitre 1, Chapitre 2)</li>
   <li>Expliquer ce qu’on voit dans ce plan (soyez bref)</li>
   <li>Expliquer l’animation qu’il y aura dans ce plan (soyez bref)</li>
</ul>
<p>Les croquis de votre scénario doivent être clairs et de bonne qualité. Pas de barbouillage incompréhensible.</p>

