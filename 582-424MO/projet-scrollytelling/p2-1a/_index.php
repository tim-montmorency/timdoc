<p>Voyager dans mes rêves</p>


 
 <grostitre>Création des équipes éphémères de 2 personnes</grostitre>
 <p>Pour ce projet, vous devrez partager le synopsis, moodboard, scénarimage et la création des médias (P2.1). Suite à quoi, vous allez vous séparer et continuer le projet seul pour toute la partie animation et programmation.</p>

 <grostitre>Synopsis</grostitre>

 <p>Le synopsis est un résumé du scénario, qui décrit les grandes lignes de l'histoire et qui permet de se faire une idée globale du thème et de l'évolution de l’histoire et des personnages. Il doit se borner à esquisser les principaux personnages et le déroulement dramatique, sans entrer dans les détails et ne doit comporter ni explications, ni dialogues.</p>
<p>Il doit être rédigé au présent de l’indicatif, dans un style simple, souvent indirect.</p>
<p>Exemple de synopsis :<br>
<i>Juliette (30 ans) est assise dans une salle d’attente. Une mouche tourne autour d’elle, entre dans son oreille, se pose sur sa bouche… Une guerre s’amorce entre la mouche et Juliette. Juliette finit par se débarrasser de la mouche en l’emprisonnant dans sa main.</i></p>

<warning>Rappelez-vous, la thématique est un rêve alors votre histoire peut-être quand même un peu décousue, non-linéaire, surprenante et artistique.  Il y a peu de limite en ce qui concerne l’inconscient!</warning>
