 <grostitre>Minimum requis</grostitre>

 <a href="" target="_blank" onclick="event.stopPropagation()"></a>



 
 <checklist>
Au moins 8 éléments graphiques distincts qui vont entrer / sortir de l’écran. Vous pouvez créer plusieurs éléments distincts ou plusieurs variantes d’un même élément, par exemple votre personnage principal avec des positions différentes ou des plans de vue différents.
Un décor / arrière-plan pour chaque chapitre. La taille de l'image doit être minimalement 1920x1080 ou un peu plus grand si vous prévoyez faire bouger votre arrière plan dans l'écran. Notez que : <br>• Tous les chapitres peuvent prendre place au même endroit mais pensez varier un peu le plan de cadre ou les éléments visibles de chapitre en chapitre, voir par exemple <a href="https://thebearandhisscarf.com" target="_blank" onclick="event.stopPropagation()">The Bear and his Scarf</a><br>• Vous n’êtes pas contraints à remplir l’espace en arrière-plan, si vous le désirez vous pouvez simplement ajouter quelques éléments graphiques pour mettre votre lecteur en contexte, voir par exemple <a href="https://thebearandhisscarf.com" target="_blank" onclick="event.stopPropagation()">The Bear and his Scarf</a>.
Un décor ou paysage réalisé et exporté en plusieurs couches (plusieurs images distinctes avec des zones transparente) afin de réaliser un effet parallax. Par exemple :  <a href="https://codepen.io/smnarnold/pen/BadGRPJ" target="_blank" onclick="event.stopPropagation()">Parallax avant</a> ou  <a href="https://smnarnold.com/assets/exercices/parallaxe-jupiter-apercu.mp4" target="_blank" onclick="event.stopPropagation()">Parallax planètes</a> ou  <a href="https://www.firewatchgame.com/" target="_blank" onclick="event.stopPropagation()">Parallax forêt</a>
Deux formes hyper simples pour prévoir une animation de morphing. Ces formes doivent être vectorielles et exportées en svg.
Une illustration en plusieurs couches (donc plusieurs images distinctes) pour réaliser l’animation suivante : « Quelque chose qui se compose ou se décompose en couche ou en éclats via le défilement de la page » (se référer aux <a href="https://tim-montmorency.com/timdoc/582-424MO/projet-scrollytelling/p2-1a/#requis-animations" target="_blank" onclick="event.stopPropagation()">requis des animations</a> présentés au dernier cours).
Une illustration linéaire (line art ou autre) pour réaliser l’animation suivante : « Forme (ligne) simple qui se dessine en même temps que le défilement (scroll) de la page »  (se référer aux <a href="https://tim-montmorency.com/timdoc/582-424MO/projet-scrollytelling/p2-1a/#requis-animations" target="_blank" onclick="event.stopPropagation()">requis des animations</a> présentés au dernier cours)
Deux éléments distincts animés via After Effects que vous allez exporter en 2 sprite sheets distinctes.<br>• Par exemple l’homme qui fait un cycle de marche ici :  <a href="https://www.cabletv.com/the-walking-dead" target="_blank" onclick="event.stopPropagation()">Walking Dead (scrollytelling)</a>.<br>• Attention, pas nécessaire d’être un bonhomme qui marche, ce peut-être une pyramide qui tourne sur elle-même, un poisson volant qui bat des ailes, une note de musique qui grelotte, une fleur qui fond comme un glaçon et qui ensuite se reforme. Rappelez-vous, c’est un rêve ! <br>• Le seul requis important, c’est que votre animation doit boucler de façon limpide à l’infini, donc le dernier frame doit être équivalent au premier frame. <br>• Pour l’exportation en sprite sheet <a href="https://cmontmorency365-my.sharepoint.com/:v:/g/personal/mariem_ouellet_cmontmorency_qc_ca/EUpCx6TvhglFr972pxyVSQ4BMFDWGwUahXiN7fk5O7wxpg?nav=eyJyZWZlcnJhbEluZm8iOnsicmVmZXJyYWxBcHAiOiJPbmVEcml2ZUZvckJ1c2luZXNzIiwicmVmZXJyYWxBcHBQbGF0Zm9ybSI6IldlYiIsInJlZmVycmFsTW9kZSI6InZpZXciLCJyZWZlcnJhbFZpZXciOiJNeUZpbGVzTGlua0NvcHkifX0&e=doXwwf" target="_blank" rel="noopener noreferrer"  onclick="event.stopPropagation()">voici la capsule vidéo que je vous ai prépatée</a>.
Vous pouvez en ajouter d’avantage mais cette liste regroupe les requis minimums.
