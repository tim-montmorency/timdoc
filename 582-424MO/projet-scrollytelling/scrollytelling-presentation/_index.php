<grostitre>Strollytellling : une arme de narration massive</grostitre>

<hr>
<br>
<p>Le Scrollytelling est un terme qui provient d’une contraction entre "storytelling" et "scroll", l’action de défilement "haut-bas". Jusqu’à présent, c’est surtout le secteur du journalisme (secteur qui se doit lui aussi de mener une réflexion sur les nouveaux médias comme vecteur d’informations) qui s’est emparé de cette façon de narrer les histoires.</p>

<p>C’est d’ailleurs un célèbre média américain, "The New Yorker" qui fait office de pionnier avec "<a target="_blank" href="https://www.nytimes.com/projects/2012/snow-fall/index.html#/?part=tunnel-creek ">Snow Fall: the avalanche at Tunnel Creek</a>" sorti en 2012. On peut également parler de "<a target="_blank" href="https://www.sbs.com.au/theboat ">The Boat</a>", véritable chef-d’œuvre de scrollytelling (entre les effets sonores, les dessins à l’encre de chine, les effets du texte qui tangue pendant le récit de la tempête). <strong>En fait, le principal atout du scrollytelling, c’est d’incorporer tous les éléments multimédias (sons, textes, vidéos, infographies, photos ou dessins) de façon très fluide</strong>. À quoi doit on cette fluidité ? Eh bien souvent à ce que l’on appelle les effets de parallaxe, c’est-à-dire que par l’action du scroll (donc de défilement de haut en bas) on fait arriver les différents éléments multimédia à des vitesses différentes, amenant une certaine profondeur.</p>

<p>C’est cette même fluidité qui rend la narration immersive et engageante.</p>
<p>Source: <a target="_blank" href="https://www.tree-learning.fr/tendances/le-scrollytelling-arme-de-narration-massive/">Tree-learning.fr</a></p>
