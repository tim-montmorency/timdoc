<grostitre>Notes on blindness</grostitre>
<knowmore href="https://www.oculus.com/deeplink/?action=view&path=/app/1946326588770583&ref=oculus_desktop">En 1983, John Hull, un professeur de théologie et écrivain anglais, perd définitivement la vue. Pour l’aider à comprendre et à accepter ce bouleversement, il tient pendant trois années, un journal intime d’enregistrements sur cassettes audio. Un témoignage unique pour immerger l’utilisateur dans l’expérience cognitive et émotionnelle de la cécité. La narration, la direction artistique et l’univers graphique permettent une immersion unique, complétée par un son spatialisé, une liberté de mouvement et d’interaction. </knowmore>
