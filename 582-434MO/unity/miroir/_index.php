<grostitre>Créer un miroir</grostitre>
<p>Pour créer un miroir dans Unity, il suffit de faire un clic droit dans la hiérarchie, puis de sélectionner la composante mirror dans 3D objects. Une fois le miroir ajouté à votre scène, positionnez-le selon ce que vous voulez refléter. Vous pouvez également jouer dans les paramètres du miroir pour modifier sa taille ou le rendre rond.</p>