<p class="spacer">Dans Unity, les Prefabs sont des modèles d'objets. Ils nous permettent de créer rapidement plusieurs fois le même objet.</p>


<dots></dots>

<grostitre>Création de Prefab</grostitre>
<p>Pour créer un prefab, on doit créer un GameObject dans la Hierarchy. On doit ensuite ajuster ses paramètres comme nous le voulons. Par exemple, je pourrais créer un ennemi avec un script qui l'anime lorsqu'il attaque et lorsqu'il se fait tuer. Une fois que l'objet est créé, on doit cliquer dessus dans la Hierarchy, puis le glisser dans la fenêtre Project. Assurez-vous de le glisser dans le dossier Prefab! L'objet devient alors bleu dans la Hierarchy. Cela signifie qu'il est devenu un prefab. Maintenant, vous pouvez glisser ce Prefab dans n'importe laquelle de vos scènes.</p>

<dots></dots>

<grostitre>Modifier un Prefab</grostitre>
<p>Pour modifier toutes les instances d'un Prefab, vous pouvez double-cliquer sur le Prefab dans la fenêtre project. Cela vous ouvrira un menu d'édition de Prefab. Modifiez alors tous les paramètres que vous souhaitez, puis sauvegardez. Ensuite, appuyez sur la flèche en haut à gauche de la fenêtre Hierarchy pour revenir à votre projet. Tous les prefabs devraient être modifiés.</p>
<p>Si au contraire vous voulez seulement modifier une instance du Prefab sans modifier les autres, cliquez sur ce Prefab dans la Hierarchy et modifiez ces paramètres.</p>