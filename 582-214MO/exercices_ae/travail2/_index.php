<grostitre>Contraintes visuelles de l'animation 2D - générique</grostitre>
<checklist>
    Réaliser et animer un générique de type carton et non un générique défilant, c'est à dire un carton (un texte) à la fois.
    Vous devez utiliser des couleurs monochromes et respecter le style graphique de votre projet. 
    Vous devez animer l’entrée et la sortie de chaque texte. 
    Vous devez animer l’arrière-plan.  
    Respecter les safe title. 
</checklist>
<dots></dots>
<grostitre>Contraintes techniques de l'animation 2D - générique</grostitre>
<p>Vous devez utiliser au moins 2 techniques d'animation parmis les suivantes (différentes de vos techniques pour la mosaïque):</p>
<checklist>
    Animation de masque
    Particules 
    Animations liquides
    Onde progressive
    Animation de formes concentriques
</checklist>
