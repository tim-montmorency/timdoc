<p>Équipe de 4</p>

<p>Lorsque vous trouvez une réponse, À l'aide d'un de vos cellulaires, vous devez prendre une photographie 📷 d'un des membres de l'équipe à côté de la réponse (on doit la voir clairement sur la photo) et la personne sur la photo doit montrer le # de la question avec ses doigts ☝️ ✌️ 🖐. Si la réponse ne se montre pas visuellement, prenez un petit clip vidéo de la personne qui mentionne verbalement la réponse (n’oubliez pas le # de la question avec les doigts).</p> 

<p>Quand vous pensez avoir toutes les bonnes réponses, revenez au local B1372 (là où nous avons mangé de la pizza), nous allons valider vos réponses. Si erronée, nous vous renvoyons trouver la bonne réponse. Première équipe qui a trouvé toutes les réponses gagne!</p>

<p>Le rallye prend place qu'à l'étage 1, des bloc B, C et D. Inutile d'aller dans les autres blocs du collège ou sur les autres étages.</p>

<p>Vous avez max 30 minutes pour le compléter à partir de maintenant. Si dans 30 minutes, vous n'avez pas trouvé toutes les réponses, revenez ici. S'il n'y a pas déjà une équipe gagante déclarée, nous allons compiler et voir quelles équipes aura le mieux performé.</p>

<p>On joue pour le plaisir de découvrir les espaces et apprendre à se connaitre un peu.<br>🧑🏽👧🏻👨‍🦰👩🏾‍🦳🧔🏻‍♀️🧑🏼‍🦱</p>

