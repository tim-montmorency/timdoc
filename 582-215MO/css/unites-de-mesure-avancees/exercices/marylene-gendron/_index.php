<h3>Médias</h3>

<mediafile src="images/heros.jpg">Héros</mediafile>

<br>

<info>En web, un héros est une zone contenue prenant toute la largeur d'une page et une partie importante de la hauteur de celle-ci. Il est généralement utilisé en début de page afin de définir l'ambiance désirée. Il se caractérise par une vidéo ou une image d'arrière-plan et la superposition d'un texte par-dessus celle-ci.<br><br>Par exemple, voyez la <a target="_blank" href="https://www.adidas.ca/fr">page d'accueil d'Adidas</a>.</info>

<dots></dots>


<grostitre>Requis</grostitre>

<checklist>
    Faites en sorte que l'élément héros ait un fond noir ⚫️ et un texte de couleur blanc ⚪️. Il devra prendre toute la largeur de la page, ainsi que la moitié de la hauteur de la fenêtre.
    Attribuez ensuite au héros l'image d'arrière-plan fournie. Assurez-vous que celle-ci couvre l'ensemble du héros et que le visage de Marylène reste bien visible, peu importe la résolution de la fenêtre.
    L'article doit être centré horizontalement ↔️, avoir une largeur correspondant à 60% de celle de la fenêtre et avoir une marge intérieure de tous les côtés équivalente à 2% de la largeur de la fenêtre.
    Faites en sorte que la taille de base de tous les textes correspond à 2% de la largeur de la fenêtre. Ainsi, le texte devrait se redimensionner proportionnellement avec la taille de ladite fenêtre.
    Une fois l'article de base terminé, créez maintenant un <em>"blocker"</em> par-dessus celui-ci, réduisant l'accès à l'article pour les lecteurs non connectés. Pour ce faire, faites en sorte que l'élément <em>"blocker"</em> prenne toute la dimension de la page et ait une couleur de fond noir semi-transparente ⚫️.
    À l'intérieur du <em>"blocker"</em>, faites en sorte que le message ait un fond noir délavé ⚫️, un texte blanc ⚪️, une largeur équivalente à 40% de celle de la page et affichez-le dans le coin inférieur droit de l'écran ↘️.
    Finalement, empêcher la page de défiler en coupant tout ce qui dépasse de celle-ci.
</checklist>

<bravo>Ça y est, vous savez maintenant comment plusieurs sites implémentent un <em>“blocker”</em> sur leurs articles!</bravo>

