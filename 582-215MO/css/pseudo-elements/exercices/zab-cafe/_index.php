<grostitre>Requis</grostitre>

<checklist>
    Faites en sorte que tous les liens soient jaunes 🟡 et que leur effet souligné disparaisse lorsqu'ils sont survolés.
    Créez des pseudo-éléments ayant une dimension correspondant à celle d'une lettre <em>(avant ou après le lien devant contenir une icône)</em>. Faites attention de laisser un espace équivalent à 1/4 de lettre entre le texte et son icône.
    Attribuez aux pseudo-éléments l'icône correspondant à leur type en image d'arrière-plan.<br><br>Afin d'attribuer les bonnes icônes, vous devrez avoir recours aux sélecteurs CSS avancés. Réviser les 3 derniers niveaux de <a onclick="event.stopPropagation();" target="_blank" href="https://flukeout.github.io/">CSS Diner</a> est probablement une bonne idée.<br><br>Une seule règle de style par type de lien devrait permettre de styliser à la fois les liens affichés en liste, ainsi que ceux dispersés dans le texte.
</checklist>

