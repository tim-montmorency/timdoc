<grostitre>Requis de base</grostitre>

<checklist>
  Le fond de la page doit avoir un dégradé allant de <em>orange</em> à <em>jaune</em>.
  L'élément <span class="inline-code">.wrapper</span> doit avoir une marge intérieur top de 30px, avoir une largeur de 200px et être centré horizontalement dans la page.
  Les images doivent être centrées horizontalement à l'intérieur de leur parent <span class="inline-code">.wrapper</span>.
  Les images des personnages doivent avoir une hauteur de 150px, forçant ainsi un affichage vertical.
</checklist>


