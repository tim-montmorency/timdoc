<grostitre>Requis</grostitre>

<checklist>
  Faites en sorte que la couleur de fond du 1er carré devienne bleue lorsque celui-ci est survolé.
  Lorsque le 2e carré est survolé, le plus petit cercle à l'intérieur de lui doit devenir bleu.
  À l'intérieur du 3e carré, changez la couleur de fond du cercle intermédiaire à bleu lorsque celui-ci est survolé.
  Lorsque le 4e carré est survolé, changez sa couleur de fond, ainsi que celle de tous les cercles à l'intérieur de celui-ci afin qu'ils deviennent bleus.
