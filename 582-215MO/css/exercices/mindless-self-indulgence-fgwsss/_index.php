<grostitre>Requis</grostitre>

<checklist>
  Ajoutez la couleur d'arrière plan rose foncé.
  L'album doit avoir une dimension de <em>500x500px</em> et être parfaitement centré horizontalement dans la page.
  Un dégradé vertical allant de gris foncé à bleu avec un point d'arrêt à 25% doit remplir le fond de l'album.
  Utilisez <em>@font-face</em> afin de charger les deux polices d'écriture.
  Le nom du groupe doit être centré horizontalement, utiliser la police <em>Bitwise</em>, avoir une grosseur de <span class="inline-code">36px</span>, un espace de <span class="inline-code">0.04em</span> entre les caracères, une bordure intérieure top de <span class="inline-code">25px</span> et être de couleur rose.
  Utilisez un double <span class="inline-code">text-shadow</span> afin de créer un effet de stoke de <span class="inline-code">0.5px</span> jaune.
  Le titre de l'album doit être aligné à droite, utiliser la police <em>Chophouse</em>, avoir une grosseur de <span class="inline-code">17px</span>, une hauteur de ligne aussi de <span class="inline-code">17px</span>, une marge extérieur top de <span class="inline-code">4px</span> et être de couleur vert.
  Utilisez les propriétés de background afin d'y ajouter les <em>Frankenstein Girls</em> 2 et 3. La #2 doit être alignée en bas à gauche et avoir une hauteur de <span class="inline-code">315px</span>. La #3 doit être alignée en bas à droite et avoir une hauteur de <span class="inline-code">255px</span>. 
  Finalement, utilisez la balise <span class="inline-code">img</span> afin de placer la <em>Frankenstein Girl</em> 1. Elle doit être aligné dans le bas et 86px à gauche ainsi qu'avoir une hauteur de <span class="inline-code">464px</span>.
</checklist>

