<grostitre>Requis</grostitre>

<checklist>
  Téléchargez une copie de l'image <em>boule-disco.png</em>. Placez-la dans un dossier intitulé <span class="inline-code">images</span> dans votre projet et utilisez cette image plutôt que celle en ligne.
  Le fond de la page doit être un dégradé vertical partant de <em>brun pâle</em> à <em>jaune</em>.
  L'album doit être de couleur <em>brun moyen</em>, avoir une dimension de 500px par 500px et être parfaitement centré horizontalement. <em>(Attention, votre page ne doit pas avoir de barre de défilement visible.)</em>
  Chaque layer doit avoir un <span class="inline-code">border-radius</span> de 25% sur son coin inférieur gauche et être positionné dans le coin supérieur droit.
  Le layer <span class="inline-code">no1</span> doit avoir une dimension de 385px par 355px et être de couleur <em>brun moyen</em>.
  Le layer <span class="inline-code">no2</span> doit avoir une dimension de 415px par 405px et être de couleur <em>blanc cassé</em>.
  Le layer <span class="inline-code">no3</span> doit avoir une dimension de 435px par 425px et être de couleur <em>jaune</em>.
  Le layer <span class="inline-code">no4</span> doit avoir une dimension de 450px par 460px et être de couleur <em>brun foncé</em>.
  Importez la police d'écriture <em>Besley ExtraBold 800</em> à l'aide de la balise <span class="inline-code">link</span> et appliquez la à l'album.
  Le nom du groupe doit doit avoir une grosseur de 24px, être de couleur <em>blanc cassé</em>, avoir une marge extérieur gauche de 64px et une marge extérieur top de 122px.
  Le titre de l'album doit avoir une grosseur de 14px, être de couleur <em>jaune</em>, avoir une marge extérieur gauche de 64px et une marge extérieur top de -4px.
  Le layer de boule disco doit avoir une dimension de 180px par 143px et être positionné à 38px de la droite et 156px du haut.
  Appliquez l'image <em>boule-disco.png</em> à l'arrière plan du layer de boule disco. Attention, l'arrière plan doit être contenu.
