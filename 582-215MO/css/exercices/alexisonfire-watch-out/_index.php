<grostitre>Requis de base</grostitre>

<checklist>
  Téléchargez les images et placez-les dans un dossier nommé <span class="inline-code">images</span>.
  Téléchargez la police d'écriture et placez-la dans un dossier nommé <span class="inline-code">fonts</span>.
  Le fond doit être un dégradé allant de <em>bleu foncé</em> à <em>bleu pâle</em> avec un point d'arrêt final à <em>200%</em>.
  L'album doit avoir une dimension de <em>500x500px</em>, être de couleur <em>verte</em> et avoir un ombrage de <em>100px</em> égale de tout les côtés de couleur <em>noire</em> semi-transparente.
  Utilisez la propriété <span class="inline-code">transform</span> afin de centrer l'album horizontalement et verticalement dans la fenêtre.
  Avec <span class="inline-code">@font-face</span>, importez la police d'écriture <em>Lunisolar</em>.
</checklist>

<grostitre>Requis arrière-plan</grostitre>

<checklist>
  L'illustration doit être centrée horizontalement et être positionnée à <em>140px</em> du haut.
  Le coeur doit être positionné à <em>410px</em> de la gauche et <em>60px</em> du haut.
  Le fantôme gauche doit être positionné à <em>40px</em> de la gauche et <em>205px</em> du haut.
  Le fantôme droit doit être positionné à <em>325px</em> de la gauche et <em>210px</em> du haut.
</checklist>

<grostitre>Requis Watch Out!</grostitre>

<checklist>
  Le nom de l'album doit être positionné à <em>190px</em> de la gauche et <em>125px</em> du haut.
  Appliquez-lui la police d'écriture <em>Lunisolar</em> à <em>70px</em> de grosseur et de couleur <em>jaune</em>.
  Il doit aussi avoir un contour de <em>2px</em> de couleur noire.
  Utilisez <span class="inline-code">transform</span> pour lui appliquer un scaleX de <em>50%</em>.
</checklist>

<grostitre>Requis Alexisonfire</grostitre>

<checklist>
  Le nom du groupe doit être positionné à <em>50px</em> de la gauche et <em>30px</em> du haut.
  Appliquez-lui la police d'écriture <em>Lunisolar</em> à <em>72px</em> de grosseur avec un style <i>italic</i>, un espace de lettrage de <em>1px</em> et de couleur <em>rose</em>.
  Il doit aussi avoir un contour de <em>1px</em> de couleure <em>noire</em> et un ombrage plein de <em>2px</em> vers la droite aussi de couleur noire.
  Utilisez <span class="inline-code">transform</span> pour lui appliquer un <em>scaleY</em> de <em>175%</em>, un <em>rotateY</em> de <em>15deg</em> et un <em>skewY</em> de <em>0.5deg</em>.
</checklist>

