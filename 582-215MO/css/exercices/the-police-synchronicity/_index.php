<grostitre>Requis</grostitre>

<checklist>
  La page doit avoir un fond vert 🟢, tandis que l'album doit avoir une largeur équivalente à la moitié de la page, être centré horizontalement ↔️ et avoir un fond blanc ⚪️.
  Chaque bloc de texte doit être noir, utiliser la Goole font <em>Permanent Marker</em>, avoir une taille de police équivalente à 2% de la largeur totale de la fenêtre et une marge intérieure de tous les côtés correspondant à une demi-lettre.
  Le nom du groupe <em>(The Police)</em> doit avoir une ombre bleue 🔵 de 2px vers le bas et la droite ↘️ alors que le titre de l'album <em>(Synchronicity)</em> doit avoir une ombre identique, mais rouge 🔴.
  Créez trois <span class="inline-code">div</span> correspondant aux trois bandes de collage photo de l'album. Ces <span class="inline-code">div</span> doivent prendre toute la largeur de l'album et avoir un ratio de 10 de largeur par 3 de hauteur <em>(10/3)</em>.<br><br>Le simple fait d'ajouter ces <span class="inline-code">div</span> dans le flux <em>(flow)</em> de l'album devrait lui donner sa hauteur.
  Ajoutez ensuite dans ces <span class="inline-code">div</span> une balise image contenant l'image du collage adéquat. Cette image doit prendre toute la largeur et la hauteur de sa bande.<br><br>Attention, certaines de ces images contiennent des icônes de policiers 👮‍♂️sur fond vert 🟢. Ces icônes ne doivent pas être visibles. Il faut donc ajuster les images afin que ceux-ci ne soient pas visibles et que les images ne soient pas déformées.<br><br><img src="images/the-police-icon.png">
  Une fois vos collages affichés correctement, ajoutez dans chacune des bandes le splash de couleur qui lui est associé et faites en sorte que le collage photo soit visible au travers de celui-ci.
</checklist>

