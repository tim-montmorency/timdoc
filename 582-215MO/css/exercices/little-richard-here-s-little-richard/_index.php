<grostitre>Requis</grostitre>

<checklist>
  L'album doit avoir une dimension de <em>500x500px</em> et être parfaitement centré horizontalement dans la page.
  Une bordure blanche de 30px doit être présente tout le tour de l'album. <em>(Attention cette bordure ne doit pas changer la dimension totale de l'album.)</em>
  Un dégradé en diagonal ↗️ allant d'orange dans le coin inférieur gauche jusqu'à jaune dans le coin supérieur droit doit remplir le fond de l'album.
  La photo de M. Richard doit prendre <em>60%</em> de la largeur de l'album, ne pas être déformée et être positionné dans le coin inférieur gauche ↙️.
  Le titre de l'album doit être entièrement dans une balise <span class="inline-code">h1</span>, avoir une taille de 60px et utiliser la police Anton. Le texte doit être inscrit en minuscule dans le HTML et une propriété CSS doit le transformer en majuscule. Ajuster l'espace verticale entre les lignes via la propriété <span class="inline-code">line-height</span>.
  Utilisez 3 balises <span class="inline-code">span</span>, une pour <em>"Here's"</em>, <em>"Little"</em> et <em>"Richard"</em> afin d'ajuster leur couleur et les décaler.
</checklist>

