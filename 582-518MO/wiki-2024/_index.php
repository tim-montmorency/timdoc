    <grostitre>Présentation des designs</grostitre>
    <h3>Temps alloué</h3>
    <ul>
        <li>Présentation 5 min.</li>
        <li>Questions, commentaires, critiques et réponses: 2 min.</li>
        <li>Vote en classe: 1 min.</li>
        <li>Changement au prochain: 1 min.</li>
    </ul>


    <h3>Prise de notes</h3>
    <p>Chaque équipe doit désigner une personne qui prendra des notes pendant les présentations des 2 autres membres. Évidemment, une 2e personne devra prendre des notes lorsque la première est en train de présenter. Ces notes doivent être envoyées dans le canal Teams de votre équipe d'ici la fin de la journée à 23h59 ce soir (prenez le temps de mettre ça au propre et de corriger les fautes).</p>

    <h3>Processus de la présentation des maquettes</h3>
    <ul>
        <li>Ordre des équipes au hasard, puis si nécessaire ordre des étudiants dans l'équipe tiré aussi au hasard.</li>
        <li>Chaque personne de l'équipe présente:
            <ul>
                <li>Présentation du moodboard, maquette et guide de styles.</li>
                <li>Période de commentaires et questions provenant du clients et/ou du board de direction (comité
                    conseil)</li>
            </ul>
        </li>
        <li>Après les la présentations des 2 ou 3 membres de l'équipe en cours, tous les étudiant font "partie du board de direction" avec moi (le client) et doivent voter pour la meilleure maquette lors d'un vote anonyme en ligne. </li>
    </ul>

    <h3>Présentation orale de votre design.</h3>
    <p>Quoi dire lors de la présentation au client (il y aura le client son comité qui analyseront votre travail,
        commenteront, questionneront et prendraient une décision) : </p>
    <ul>
        <li>Présentez dans cet ordre : moodboard, maquette (design) de l'accueil et ensuite guide de styles.</li>
        <li>Expliquez votre démarche.</li>
        <li>Expliquez :
            <ul>
                <li>Votre choix de couleurs (psychologie des couleurs apprises en communication graphique).</li>
                <li>Votre choix de style graphique en fonction de la clientèle visée par le client.</li>
                <li>La navigation simplifiée en fonction de l’objectif du site (mise en évidence de certains éléments
                    etc.)</li>
                <li>L’utilisation du CRAP. Contraste, Répétition, Alignement, Proximité.</li>
                <li>Iconographie (si utilisation d’icônes) </li>
                <li>La mise en contraste de certains éléments importants. Pourquoi et comment?</li>
                <li>Vous devez convaincre votre client que votre design c’est le meilleur, si vous n’êtes pas convainquant, vous perdrez des points. Croyez en votre travail et présentez-le avec fierté, c’est l’objectif ici&nbsp;! C’est une simulation de la vraie vie en entreprise et c’est ce qui importe à ce stade-ci. Foncez&nbsp;!</li>
            </ul>
        </li>
    </ul>

    <h3>Board de direction (comité conseil)</h3>
    <p>Chaque étudiant DOIT faire un commentaire constructif/pertinent au moins 2 fois pendant la période. C'est important que vous appreniez à formuler et à recevoir des critiques comme des professionnels. Je prendrai des notes de qui
va le faire ou pas. Si vous ne le faites pas, vous allez être pénalisés sur votre évaluation aujourd'hui, c'est un des critères d'évaluation de pouvoir poser un regard critique sur le travail d'un pair.</p>

