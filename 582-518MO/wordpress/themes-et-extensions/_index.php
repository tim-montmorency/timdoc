<p>WordPress propose une interface simple et efficace, permettant facilement l'ajout de thèmes et&nbsp;d'extensions.</p><p>Pour ce faire, il faut premièrement se connecter au tableau de bord. Normalement, il suffit d'ajouter&nbsp;<incode>/wp-admin</incode>&nbsp;à l'url du site en question pour être redirigé vers&nbsp;l'admin et ensuite d'entrer ses informations de connexion:&nbsp;<em>(nom d'utilisateur/mot de&nbsp;passe)</em>.</p>


<dots></dots>
<grostitre>Thèmes</grostitre>
<p>Il suffit dans la barre latérale de l'admin de&nbsp;sélectionner:</p>

<p><strong>Apparence</strong>&nbsp;➡️&nbsp;<strong>Thèmes</strong></p>

<p>Certains thèmes par défauts seront disponibles. Il est néanmoins possible d'en ajouter plus. Pour ce faire, cliquez sur le bouton&nbsp;<em>Ajouter</em>&nbsp;se trouvant à la droite du titre de la&nbsp;page.</p>

<p>Plusieurs thèmes réalisés par d'autres développeurs seront alors proposés. Si l'un d'entre eux correspond à vos besoins, il suffit de cliquer sur&nbsp;<em>Installer</em>&nbsp;et ensuite&nbsp;<em>Activer</em>&nbsp;pour qu'il devienne le thème&nbsp;courant.</p>

<p>Il est aussi possible d'ajouter un thème via un fichier&nbsp;<em>.zip</em>&nbsp;en appuyant sur&nbsp;<em>téléverser un thème</em>. Cette option est favorisée par les thèmes payants afin de restreindre l'accès au code, mais surtout, elle permet de téléverser un thème créé par&nbsp;soi-même!</p>

<p>Installons un&nbsp;<a href="../medias/theme-de-base.zip">thème de base</a>&nbsp;à partir de cette dernière méthode afin de bien comprendre le fonctionnement de&nbsp;WordPress.</p>

<dots></dots>
<grostitre>Extensions</grostitre>
<p>Tout comme les&nbsp;<a rel="noopener noreferrer" href="#themes">thèmes</a>, il suffit dans la barre latérale de l'admin de choisir&nbsp;<em>Extensions</em> (plugins)&nbsp;et de décider si nous souhaitons choisir parmi certaines extensions existantes ou si nous désirons en téléverser&nbsp;une.</p><p>Dans le cadre de ce cours, je vous invite à ajouter deux extensions existantes. Pour ce faire, cliquer dans la barre intitulée&nbsp;<em>Rechercher des&nbsp;extensions</em>:</p>

