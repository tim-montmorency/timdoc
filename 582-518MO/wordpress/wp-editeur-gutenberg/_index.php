<p>Depuis WordPress&nbsp;5, l'éditeur de mise-en-page par bloc est inclu dans le coeur de WordPress. Cet éditeur se nomme Gutenberg qui est  le nom de l'inventeur de l'imprimante.</p>

<img src="images/ecran-editeur-gutenberg-wp.jpg" alt="Impression écran de l'éditeur Guterberg." style="box-shadow: 5px 5px 20px rgba(0,0,0,0.2);">

<dots></dots>

<grostitre>Usage</grostitre>
<p>Éditeur avec boîte à outils intégré qui vous permet de réaliser une mise-en-page perosnnalisée sans coder ni HTML, ni CSS.</p>

<p>Attention, il ne faut pas confondre un modèle (<em>template</em>) et l'utilisation de la mise en page par l'éditeur Gutenberg. Ce dernier sert essentiellement à la mise-en-page du contenu d'un article sprécifique et le tout se place un peu "à la main". Par exemple si je souhaite inclure une image au milieu d'un article et l'afficher à côté d'un paragraphe, je peux utiliser cet éditeur pour créer deux colonnes et y placer mon contenu, à droite l'image, à gauche mon paragraphe.</p>

<p>Notez que lorsque vous créez plusieurs colonnes avec Guternberg, elles s'affichent automatiquement l'une sous l'autre en format tablette ou en format mobile. Bref, c'est <em>responsive</em>&nbsp;!</p>

<p>Vous pouvez aussi créer des styles de bloc qui pourront être réutilisable de page en page.</p>
<img src="images/ecran-editeur-gutenberg-wp-bloc-reutilisable.jpg" alt="bloc réutilisable">


<warning>Notez bien que la mise en place de ce qui est autour du contenu de l'article (le titre, l'auteur, la date de publication, l'entête de la page web, le pied de page, l'ajout de publicité, autres articles suggérés etc) se fera via le modèle (<em>template</em>) du thème lié au type de contenu (par exemple le modèle page.php ou single.php du thème actif) et non pas via cet éditeur.</warning>



