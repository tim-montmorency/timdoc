 <p>L'optimisation pour les moteurs de recherche, également appelée&nbsp;<em>Search Engine Optimisation (SEO)</em>, inclut l'ensemble des techniques qui visent à améliorer le positionnement d'un site web dans la page de résultats des moteurs de recherche, tels que:&nbsp;<a href="https://www.google.ca/" target="_blank">Google.com</a>,&nbsp;<a href="https://ca.yahoo.com/?p=us" target="_blank">Yahoo.com</a>,&nbsp;<a  target="_blank" href="https://www.bing.com/">Bing.com</a>,&nbsp;<a href="https://duckduckgo.com/" target="_blank">DuckDuckGo.com</a>, <a href="https://www.qwant.com/<" target="_blank">Qwant</a>, <a href="https://www.ecosia.org" target="_blank">Ecosia</a> etc.</p>



<dots></dots>
<grostitre>Installation</grostitre>
<p>Dans l'onglet extensions&nbsp;<em>(plugins)</em>&nbsp;du tableau de bord, recherchez&nbsp;<em>Yoast SEO</em>&nbsp;et repérez l'extension créée par Team&nbsp;Yoast.</p><p>Installez et activez&nbsp;là.</p>

<img src="images/yoast-seo-preview.jpg" alt="Aperçu de Yoast">




<dots></dots>
<grostitre>Assistant de configuration</grostitre>

<p>Une fois installé, un onglet&nbsp;<em>SEO</em>&nbsp;devrait être visible dans le menu&nbsp;latéral.</p>

<p>À partir de cet onglet, il est possible d'accéder à l'assistant de configuration en cliquant sur&nbsp;<em>Général</em>.</p>

<img src="images/yoast-seo-menu-barre-gauche.jpg" alt="SEO dans la barre lattérale gauche pour ouvrir Yoast SEO.">


 <p>Le premier élément visible devrait-être le bloc&nbsp;<em>Première configuration SEO</em>. En cliquant sur le lien&nbsp;<em>assistant de configuration</em>&nbsp;à l'intérieur de celui-ci, vous devriez être redirigé vers un formulaire vous permettant de configurer les options SEO de base de votre&nbsp;site.</p>

