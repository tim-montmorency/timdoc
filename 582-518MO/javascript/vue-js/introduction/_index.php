<p>Vue est un cadriciel JavaScript créé par Evan You <em>(le créateur de Vite)</em> permettant de rapidement et simplement développer des sites web ou applications de petites ou grandes envergures. </p>

<p>Sa grande force réside dans sa fonctionnalité de <em>"two-way binding"</em> permettant de mettre à jour automatiquement différentes informations dans une interface en fonction de la valeur de certaines propriétés.</p>




<dots></dots>
<grostitre>Versions précédentes</grostitre>
<p>La version courante et enseignée de Vue dans ce cours est <a target="_blank" rel="noopener noreferrer" href="https://v3.vuejs.org/guide/introduction.html">la version 3</a>. Cependant, il est probable que vous trouviez beaucoup de documentation concernant la version 2, puisque celle-ci était la plus récente jusqu'à la fin de l'année 2020. </p>

<p>Si vous lisez sur le sujet en ligne, faites attention de vous assurer que l'article parle bien de la version 3, car certaines différences importantes entre les deux versions pourraient vous causer des enjeux.</p>


