<p>Vue est un cadriciel progressif JavaScript permettant de rapidement et simplement développer des interfaces utilisateur dynamiques (UI) pour sites web ou des applications web de petites ou grandes envergures. </p>

<p>Sa grande force réside dans sa fonctionnalité de <em>"two-way binding"</em> permettant de mettre à jour automatiquement différentes informations dans une interface (View) en fonction de la valeur de certaines propriétés du modèle (Model) et vice-versa.</p>
<p><a href="https://www.geeksforgeeks.org/vue-js-two-way-binding-model/" target="_blank" rel="noopener noreferrer">Voici un exemple concret du <em>"two-way binding"</em></a>.</p>


