<p>La mêlée quotidienne, aussi connu en anglais sous le nom de <em>“daily scrum”</em>, est le moment où une équipe se regroupe afin de prendre du recule face à son projet. Ceci permet à tous les membres de mesurer l’avancement accompli, de se mettre à jour face aux enjeux rencontrés et de s’adapter afin de réussir à livrer son&nbsp;projet.</p>

<p>De par son nom, on devine qu'en temps normal la mêlée quotidienne a lieu tous les jours. Cependant, dans le cadre de ce cours, nous prendrons en compte le fait que vous avez d'autres engagements et ne vous demanderons de faire qu'une mêlée hebdomadaire <em>(weekly scrum)</em>.</p>



<dots></dots>
<grostitre>Déroulement</grostitre>
<ul>
    <li>Ces rencontres peuvent-être fait en personne ou en ligne. <strong>Idéalement, les rencontres en personne sont privilégiées</strong>, mais non obligatoires.</li>

    <li>Ces rencontres peuvent avoir lieu à <strong>n'importe quelle heure du jour</strong>, malgré qu'on remarque une forte tendance vers le début de la journée dans l'industrie.</li>

    <li>Chaque membre de l'équipe prend la parole un après l'autre. <br>Lors de votre tour, mentionnez: 
    
    <ul>

        <li>🌹 <strong>Quels sont vos accomplissements depuis la dernière rencontre?</strong> <br>En répondant à cette question, les membres de l'équipe partage aux autres progrès, où ils sont rendus dans l'avancement de leur tâche assignée. De plus en partageant la tâche accomplie, cela peut donner aux membres de l'équipe un sentiment d'accomplissement.✨<br>
        Si certains membres de l'équipe travaillent sur des éléments qui ne sont pas alignés avec les objectifs, c'est le moment de le souligner en groupe et de réaligner le tir pour chacun.👨‍⚖️</li>
        
        <li>🚀 <strong>Sur quoi prévoyez-vous travailler d'ici la prochaine?</strong> <br>Aide votre équipe à se synchroniser afin que personne n'accomplisse la même tâche en même temps.<br><br><img src="images/dilbert_redondance.png" alt="">Permet aussi de planifier les tâches à venir. <br><br>Par exemple, Garfunkel prévoit faire l'intégration du menu dans WordPress d'ici le prochain cours. Simon peut donc planifier débuter son habillage CSS dès ce moment.</li>
        
        <li>🌵 <strong>Qu'est-ce qui vous empêche d'avancer en ce moment?</strong> <br>Donne de la visibilité à votre équipe sur vos obstacles. Peut-être un coéquipier peut-il vous aider? Peut-être est il temps de laisser cet élément de côté si vous bloquez depuis longtemps, etc.</li>
        
        <li>🎉 <strong>Qu'est-ce qui vous motive en ce moment sur le projet?</strong> <br>Ce point est optionnel, mais est encouragé. Par exemple: un coéquipier vous a débloqué sur une tâche, vous avez compris un concept intéressant, vous, vous sentez en confiance pour une démo, un élément qu'a ajouté un collègue améliore grandement le site. Soulignez ces petites victoires!</li></ul></li>
    
        <li>Le PO (Product Owner) est aussi sensé profiter de cette rencontre afin de <strong>s'assurer que les cartes du tableau sont à jour</strong> en fonction de ce que les membres de l'équipe mentionnent. Si ce n'est pas le cas, il doit leur rappeler de les mettre à jour. </li>
</ul>




<dots></dots>
<grostitre>Règles générales</grostitre>

<ul>
    <li><p>Chaque personne doit <strong>être à l'heure</strong>. Une mêlée est censée être rapide, il est facile de doubler son temps si une ou plusieurs personnes arrivent en retard.</p></li>
    <li><p>L'équipe doit être focusée sur la mêlée. <strong>Personne ne doit faire autre chose en parallèle</strong> <em>(consulter son cellulaire, jouer à un jeu, etc.)</em>.</p></li>
    <li><p>Votre tour de parole ne sert pas à informer vos collègues sur votre vie personnelle <em>(vos plans pour le week-end, l'anecdote la plus récente concernant votre animal de compagnie, etc.)</em>. <strong>Tenez-vous-en au projet</strong>.</p></li>
    <li><p>Un tour de parole <strong>devrait durer 1 à 2 minutes par personne</strong>. Si un point déborde, planifiez une rencontre afin de discuter en détail de ce point ou attendez à la fin de la mêlée avant d'aborder à nouveau le sujet afin de libérer les personnes non impliquées directement.</p></li>
    <li><p><strong>Le scrum master dirige la mêlée</strong>, lorsqu'une des règles générales est enfreinte, celui-ci doit ramener son équipe à l'ordre.</p></li>
</ul>



<dots></dots>
<grostitre>Requis</grostitre>
<checklist>
Dans le cadre du projet intégrateur, vous devrez tenir une mêlée par semaine. Celle-ci doit se tenir <strong>toujours être à la même heure et au même jour de la semaine</strong>. Pour faciliter votre tâche, je vous propose de toujours tenir cette rencontre à un moment que je vais définir *pendant le cours du lundi*.
Après vous être rencontré, après chaque mêlée, chaque membre de l'équipe doit écrire un résumé de son tour de parole à l'intérieur du canal de son équipe Teams dans le format suivant:<br>-&nbsp;🌹 Éléments terminés<br>-&nbsp;🚀 Sur quoi comptez-vous travailler<br>-&nbsp;🌵 Problèmes rencontrés<br>-&nbsp;🎉 Ce qui vous motive en ce moment/félicitations<br><br>Une personne peut avoir plusieurs éléments d'une même sorte.<br><br>Par exemple:<br>🌹 Héros de la page d'accueil.<br>🌹 Déploiement en ligne.<br>🚀 Commencer le pied de page.<br>🌵 Notre version du logo est pixelisée.<br>🌵 On charge actuellement deux fois GSAP?<br> 🎉 On a survécu à la démo!
</checklist>

