<p><a href="https://trello.com/">Trello</a>&nbsp;est un outil de gestion permettant de simplifier l'organisation de projets et de définir un ordre de priorité parmi les tâches à accomplir. Il est gratuit.</p>

<dots></dots>
<grostitre>Utilisation</grostitre>

<p>L'usage de Trello est très répandu. Plusieurs sites d'envergures l'utilisent, notamment:</p>
          
<ul>
    <li><a target="_blank" rel="noopener noreferrer" href="https://www.epicgames.com/store/fr/">Epic Games</a></li>
    <li><a target="_blank" rel="noopener noreferrer" href="https://stackoverflow.com/">Stack Overflow</a></li>
    <li><a target="_blank" rel="noopener noreferrer" href="https://9gag.com/">9Gag</a></li>
    <li>etc.</li>
</ul>


<dots></dots>
<grostitre>Tableau 📊</grostitre>

<p>Les tableaux donnent une vue d'ensemble sur un projet. En un coup d'oeil, il est possible de&nbsp;voir:</p>

<ul>
    <li>Qui fait quoi</li>
    <li>Ce qui est accompli</li>
    <li>Ce qu'il reste à faire</li>
    <li>etc.</li>
</ul>

<p>Il est composé principalement de 3 éléments: un tableau, des listes (colonnes) et des cartes.</p>



<h3 id="creation-tableau">Création</h3>
<p>Si c'est la première fois que vous l'utilisez, il faut d'abord créer un <em>Espace de travail</em>. Vous devez donc commencer par "Donnez un nom à votre espace de travail (nom de projet ou d'équipe)" et spécifier qui fait partie de votre équipe en les invitant (il doivent préalablement s'être créé un compte Trello pour être visibles).</p>

<img src="images/trello-espace-travail.jpg" alt="Formulaire de création d'espace de travail" style="border:1px solid #eeeeee">

<p>Pour créer un tableau, repérez dans l'entête le bouton <em>Créer</em> (ou <em>+</em> si votre écran est plus petit), cliquez sur ce bouton et sélectionnez&nbsp;<em>«&nbsp;Créer un&nbsp;tableau&nbsp;»</em>.</p><p>Vous pouvez aussi, via la page d'accueil 🏠, cliquer sur le bouton central <em>«&nbsp;Créer votre premier tableau&nbsp;»</em>.</p>

<img src="images/trello-premier-tableau.jpg" alt="Bouton de création d'un tableau Trello" style="border:1px solid #eeeeee">

<doclink href='https://trello.com/guide/trello-101#what-is-a-board'>Tableau Trello</doclink>



<dots></dots>
<grostitre>Membres de l'équipe 🤝</grostitre>
<p>La gestion des membres dans Trello permettent restreindre l'accès de certains tableaux 📊 à certains groupes d'individus. Ainsi, seuls les membres de l'espace de travail pourront consulter et modifier le contenu des tableaux créés dans cet espace spécifique.</p>

<h3 id="ajout-membres">Ajout de membres</h3>
<p>Pour inviter un membre à votre espace de travail, repérez dans le menu de la colonne de gauche, sous le nom de votre espace, le lien <em>«&nbsp;Membres +&nbsp;»</em>. Cliquez dessus et dans le champs de la modale qui apparait, cherchez la personne par son courriel associé à son Trello ou encore son nom d'usager.</p>
<img src="images/trello-invitation-membre.jpg" alt="Modal pour inviter une membre à l'espace de travail" style="border:1px solid #eeeeee">


<doclink href='https://trello.com/guide/onboard-team'>Équipes Trello</doclink>



<dots></dots>
<grostitre>Listes 📝</grostitre>

<p>Les listes permettent de grouper les cartes par catégories. Ces catégories peuvent être nommées à la discrétion de chacun afin de refléter l'état actuel des différentes tâches. Cependant, dans le cadre d'un projet numérique, ces noms proviennent généralement d'une méthodologie agile, par exemple: <a target="_blank" rel="noopener noreferrer" href="../kanban/">Kanban</a>.</p>

<p>L'idée de base de Kanban est de simplifier la visualisation de l'avancement d'un projet et de limiter le nombre d'éléments&nbsp;<em>En cours</em>. Ainsi, un développeur ne devrait avoir qu'<strong>UNE SEULE TÂCHE EN COURS</strong> d'assignée, ce qui permet a son équipe de pouvoir identifier qui travail réellement sur quelle tâche et d'identifier si cette dernière&nbsp;stagne.</p>


 <h3 id="creation-liste">Création</h3>
<p>Pour créer une liste, cliquez sur&nbsp;<em>«&nbsp;Ajouter une&nbsp;autres&nbsp;liste&nbsp;»</em>&nbsp;à droite dans votre&nbsp;tableau.</p>

<img src="images/trello-liste.jpg" alt="Bouton pour ajouter une nouvelle liste" style="border:1px solid #eeeeee">

<doclink href='https://trello.com/guide/trello-101#what-is-a-list'>Liste</doclink>




<dots></dots>
<grostitre>Cartes 🎴</grostitre>

<p>Les cartes représentent chacune des tâches à réaliser dans un projet. En cliquant, sur celles-ci, il est possible d'accéder au dos de la carte, contenant plus de&nbsp;détails.</p>


<ul>
  <li>Description</li>
  <li>Date limite</li>
  <li>Checklist</li>
  <li>etc.</li>
</ul>

<h3 id="creation-liste">Création</h3>
<p>Pour créer une carte, cliquez sur&nbsp;<em>«&nbsp;Ajouter une&nbsp;carte&nbsp;»</em>&nbsp;dans la liste&nbsp;désirée.</p>
<img src="images/trello-carte.jpg" alt="Lien visuel + Ajouter un carte" style="border:1px solid #eeeeee">

<doclink href='https://trello.com/guide/trello-101#what-is-a-card'>Carte</doclink>

<dots></dots>
<grostitre>Menus contextuels</grostitre>
<ol>
  <li>Situé à gauche du tableau, le menu permet de naviguer d'un espace de travail à l'autre ou d'un tableau à l'autre, il permet aussi de modifier les paramètres d'un espace de travail ou encore de gérer les membres qui y ont accès.</li>
  <li>En haut à droite, il y a un bouton <em>[...]</em> qui permet d'avoir accès à un menu pour personnaliser l'apparence du tableau, ajouter des champs personnalisés, gérer des automatisation, ajouter des <em>stickers</em> de réaction sur certaines cartes etc.</li>
  <li>Si vous cherchez une carte spécifique, il est possible de le faire par mot-clé dans le champs de recherche tout en haut de la page, vers la droite.</li>
</ol>
<img src="images/trello-menu.jpg" alt="Menus contextuels de l'interface Trello" style="border:1px solid #eeeeee">


