<grostitre>Le défi de l'évaluation du temps</grostitre>

<p>Dans le métier, vous aurez à faire des devis ou des évaluations de temps ou de coût pour des tâches spécifiques ou pour développer une fonctionnalité autour d'un scénario utilisateur <em>(user story)</em>.</p>

<p>Il existe plusieurs méthodes pour vous aider, mais ça reste toujours un grand défi, surtout lorsque vous n'avez pas énormément d'expérience.</p>

<p>Je me rappelle encore, lorsque j'étais au CEGEP dans le même programme que vous il y a quelques années, le professeur nous conseillait littéralement d'évaluer la tâche le plus réalistement possible et ensuite, de communiquer à notre client ou chargé de projet <em>notre évaluation préliminaire multiplié par 2</em>.<br>...<br>Ça semble assez imprécis n'est-ce pas ? Mais j'ai réalisé avec le temps qu'il avait raison, du moins sur le résultat. On a parfois tendance à sous-évaluer tous les bogues et problèmes qui peuvent surgir sur notre parcours...</p>

<dots></dots>
<grostitre>Une des solutions</grostitre>
<p>Nous en verrons une autre au prochain cours, qui ne remplace pas celle-ci mais qui la complémente.</p>
<p>La méthode de suivi de temps et l'analyse itérative qui pourrait vous aider éventuellement à être plus précis :
<ol>
    <li>Tentez d'évaluer le plus réalistement possible le travail à faire (en temps ou en $)</li>
    <li>Suivez votre temps de travail sur vos différentes tâches avec un outil approprié.</li>
    <li>Lorsque le travail est complété, prenez le temps de faire un post-portem. Comparez votre évaluation préliminaire avec votre rapport de temps réel. Analysez, apprenez, ajustez-vous pour la prochaine fois.</li>
</ol>

