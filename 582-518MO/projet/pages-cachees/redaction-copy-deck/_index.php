<p>Cette étape concerne le Project Owner <em>(PO)</em> de votre équipe. Vous êtes en charge de rédiger les Copy Decks du projet. Ces documents textes serviront de source de vérité à l’équipe tout au long du projet. À l’intérieur de celui-ci se trouve tout le contenu du site organisé par&nbsp;pages.</p>

<p>Ainsi, fini les pertes de temps à se demander: Est-ce que telle page doit-être présente sur le nouveau site? Si oui, dois-je inclure tout son contenu ou simplement une partie? Où se trouve la traduction pour telle page?&nbsp;etc.</p>


    
<dots></dots>
<grostitre>Requis</grostitre>
<checklist>Sur Word ou Google Doc, créez-vous deux documents. Un premier intitulé <em>"copy-deck-fr-v1"</em> pour la version francophone et un deuxième intitulé <em>"copy-deck-en-v1"</em> pour la version anglophone.
Dans chacun de ces documents, utilisez les options de texte de votre outil <em>(Word ou Google Doc)</em> afin de créer un titre pour chacune des pages devant être présentes sur votre site. Voir l'<a target="_blank" rel="noopener noreferrer" href="../arborescence/" onclick="event.stopPropagation()">arborescence</a> et les <a target="_blank" rel="noopener noreferrer" href="../requis-client/" onclick="event.stopPropagation()">requis clients</a>.
Insérez le contenu texte de chacune des pages dans la section lui correspondant dans vos documents. Attention de ne pas inclure accidentellement des styles en copiant/collant les textes. <em>(ctrl + shift + v agit comme ctrl + v, mais évite que les styles soient collés)</em>.
Inclure l’url de chaque page sous chaque titre de page.
S’il y a un lien à ajouter sur un élément, le mettre entre [].
Créez-vous un dossier intitulé <em>"médias"</em>. Sauvegarder à l'intérieur de celui-ci chacune des images de contenu des pages incluses dans la refonte de votre site. Incorporez dans vos documents ces images au bon endroit.
Complétez tous les textes anglophones manquant en les traduisant à l'aide de <a target="_blank" rel="noopener noreferrer" href="https://translate.google.com/?hl=fr&amp;sl=fr&amp;tl=en&amp;op=translate" onclick="event.stopPropagation()">Google Traduction</a> ou <a href="https://www.deepl.com/" target="_blank" rel="noopener noreferrer" onclick="event.stopPropagation()">DeepL</a>. La traduction n'a pas besoin d'être parfaite. Pour la page d'équipe, complétez au besoin avec du <a target="_blank" rel="noopener noreferrer" href="https://www.lipsum.com/feed/html" onclick="event.stopPropagation()">Lorem Ipsum</a> et <a target="_blank" rel="noopener noreferrer" href="https://thispersondoesnotexist.com/" onclick="event.stopPropagation()">ThisPersonDoesNotExist</a>.
Générez une table des matières au début de vos documents et exportez-les en format PDF.
Ajoutez vos fichiers PDF ainsi que votre dossier <em>"médias"</em> à l'intérieur du dossier intitulé <em>"sources"</em> de votre projet sur GitHub.</checklist>
    



<dots></dots>
<grostitre>Mise à jour</grostitre>
<p>Au besoin, créez de nouvelles versions de vos Copie Deck. Par exemple, si vous trouvez des erreurs. Le cas échéant, téléversez une nouvelle version nommée <em>"v2"</em> ou <em>"v3"</em>, tout dépendant d'où vous êtes rendu dans vos versions de Copy Deck et avisez votre équipe.</p><p>Ainsi, si vous pouvez déterminer si un coéquipier utilise la version la plus à jour du Copy Deck en lui demandant son numéro de version.</p>


<dots></dots>
<grostitre>Remise</grostitre>
 <p>Poussez sur le repo GitHub de votre projet vos documents ainsi que votre dossier <em>"médias"</em> à l'intérieur du dossier <em>"sources"</em>.</p>