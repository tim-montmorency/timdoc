<p>Cette étape concerne le designer de votre équipe. Suite à la présentation des maquettes de la page d’accueil en classe, une maquette par équipe aura été sélectionnée par le client et son comité afin de devenir le point de départ de la&nbsp;refonte.</p>
<p>Cependant, le client et son comité auront des commentaires qui impliqueront une phase&nbsp;d’ajustements.</p>



<grostitre>Requis</grostitre>

<checklist>Suite à la réception des commentaires client, créez une nouvelle carte dans votre tableau Trello intitulé <em>"Ajustements maquette accueil"</em>.
Ajoutez chacunes des demandes dans une checklist à l'intérieur de cette carte.
Associez cette carte à vous même.
Recréez un nouveau moodboard à partir des moodboards présentés par votre équipe en ne gardant que les éléments pertinents suite au commentaires clients. <br><br>Par exemple, si vous aviez une capture d'écran démontrant un type de menu ayant été rejeté par le client, il faudra éviter d'inclure cette capture dans votre nouvelle itération.
Faites une copie du guide de styles et ajoutez V2 dans le nom afin de conserver la première version dans vos archives. Ajustez le guide de styles de sorte qu'il respecte les commentaires client. Autrement dit, présenter les bonnes couleurs, polices, etc.
Faites une copie de la maquette XD ou Figma de la page d'accueil sélectionnée et ajoutez V2 dans le nom afin de conserver la première version dans vos archives. Appliquez les ajustements à la maquette de la page d'accueil afin de tenir compte des commentaires client. 
Remplacez ensuite le moodboard, le guide de styles et la maquette <em>(XD ou Figma + PDF)</em> dans le répertoire GitHub par cette nouvelle itération.
</checklist>  
  
<warning>Assurez-vous que vos collègues approuvent vos changements, car vous devrez ensuite réaliser cette maquette&nbsp;en équipe et suivre le guide de style sur l'ensemble du site web.</warning>


<dots></dots>
<grostitre>Remise</grostitre>
<p>Il n'y a pas de remise formelle, le fait de remplacer le guide de style, le moodboard, et la maquette de l'accueil mis-à-jour sur le github fera office de remise.</p>
