<p>Pour vous inspirer, vous pouvez regarder les guides de styles en pièce jointe 👆.</p>

<info>Les exemples ne sont qu’à titre indicatif, vous n’avez aucunement à respecter cette mise en page, tant que l’information nécessaire est contenue dans votre&nbsp;document.</info>





<dots></dots>
<grostitre>Logo</grostitre>
<checklist>Inclure le logo du client. Vous devez utiliser le logo actuel où lui apporter de subtiles modifications ne le dénaturant pas.
</checklist>




<dots></dots>
<grostitre>Palette de couleur</grostitre>
 <checklist>Inclure une palette des 5 couleurs principales qui seront utilisées sur le site. L'outil <a target="_blank" rel="noopener noreferrer" href="https://coolors.co/generate">Coolors</a> peut vous aider à générer une palette harmonieuse au besoin.
 Afficher un aperçu des couleurs.
Nommez chaque couleur afin d'éviter de la confusion du type: <em>"Le vert… non l'autre vert"</em>. Si vous manquer d'inspiration, vous pouvez utiliser l'outil <a target="_blank" rel="noopener noreferrer" href="https://chir.ag/projects/name-that-color/#6195ED">Name that color</a>.
Inclure le code hexadécimal de chacune des couleurs.
</checklist>    


<dots></dots>
<grostitre>Typographie</grostitre>
<checklist>Inclure 3 niveaux de titres et nommer la police, taille, graisse, couleur et caractéristique de chacun.
Affichez un exemple de texte régulier sous forme de paragraphe. Afin d'éviter d'être distrait par le texte contenu dans le paragraphe, utilisez du <a target="_blank" rel="noopener noreferrer" href="https://www.lipsum.com/feed/html">Lorem ipsum</a>.  Nommez à nouveau la police, taille, graisse ainsi que la couleur utilisée.
Affichez le style d'un lien normal ainsi que lors de sont survole. Nommez à chaque fois la police, taille, graisse ainsi que la couleur utilisée afin de voir la différence entre les deux états.
</checklist>    


