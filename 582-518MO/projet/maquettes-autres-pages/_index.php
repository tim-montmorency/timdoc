<grostitre>Requis</grostitre>
<checklist>Les deux maquettes doivent être réalisées à partir du logiciel Figma dans le même fichier que l'accueil. 
Ces mquettes doivent être cohérente avec le guide de styles et la maquette de la page d'accueil.
Les maquettes doit respecter&nbsp;les requis clients concernant la page de <a target="_blank" rel="noopener noreferrer" href="../requis-client/#page-dune-nouvelle" onclick="event.stopPropagation()">détail de nouvelle</a> et <a target="_blank" rel="noopener noreferrer" href="../requis-client/#liste-des-services-offerts" onclick="event.stopPropagation()">liste des services</a>.
Les deux maquettes doivent être faites en deux résolutions :<br>- mobile&nbsp;<em>(375px)</em><br>- ordinateur classique&nbsp;<em>(1400px)</em>
Pour la page nouvelle, ajoutez un bloc "Articles similaires" à la fin proposant d'autres articles.
Pour la page nouvelle, ajoutez une navigation "nouvelle précédente", "nouvelle suivante" ou une pagination. À votre guise.
Pour la page nouvelle, ajoutez un style différent sur “Nouvelles” dans la navigation principale afin d'indiquer à l'usager où il se trouve.
Faites la même chose pour "Services", ajoutez un style différent sur "Services" dans la navigation principale afin d'indiquer à l'usager où il se trouve.
Inclure un lien parmi le texte de la page de nouvelle afin de visualiser le style d'un lien dans un texte courant. S'il n'y en a pas dans le texte original, créez un lien sur un mot quelconque.
Inclure aussi un italique et un gras.
Exportez une version PDF de vos maquettes.</checklist>



