<dots></dots>
<grostitre>Requis</grostitre>
<checklist>Les 2 maquettes doivent être réalisées à partir du logiciel&nbsp;Adobe XD ou Figma et être cohérente avec le guide de styles et la maquette de la page d'accueil.
La maquette doit respecter&nbsp;les requis clients concernant la page de <a target="_blank" rel="noopener noreferrer" href="../requis-client/#detail-de-nouvelle" onclick="event.stopPropagation()">détail de nouvelle</a>.
La maquette doit présenter la page de détail de nouvelle dans trois résolutions afin de montrer comment elle s'adapte sur:<br>- mobile&nbsp;<em>(375px)</em><br>- tablette&nbsp;<em>(768px)</em><br>- ordinateur classique&nbsp;<em>(1200px)</em>
Ajoutez un style différent sur “nouvelles” dans la navigation principales afin d'indiquer à l'usager où il se trouve.
Inclure un lien parmi le texte de la page de nouvelle afin de visualiser le style d'un lien dans un texte courant. S'il n'y en a pas dans le texte original, créez un lien sur un mot quelconque et dirigez le vers une autre page du site.
Inclure aussi un italique et un gras.
Exportez une version PDF de votre maquette.</checklist>











