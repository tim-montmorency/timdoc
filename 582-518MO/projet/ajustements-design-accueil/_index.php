<p>Cette étape concerne le designer de votre équipe. Suite à la présentation des maquettes de la page d’accueil en classe, une maquette par équipe aura été sélectionnée par le client et son comité afin de devenir le point de départ de la&nbsp;refonte.</p>
<p>Cependant, le client et son comité auront des commentaires qui impliqueront une phase&nbsp;d’ajustements.</p>



<grostitre>Requis</grostitre>

<checklist>Suite à la réception des commentaires client, créez une nouvelle carte dans votre tableau Trello intitulé <em>"Ajustements maquette accueil"</em>.
Ajoutez chacunes des demandes dans une checklist à l'intérieur de cette carte.
Associez cette carte à vous même.
Si ce n'est pas déjà le cas, pour la maquette gagante de votre équipe, rassemblez dans le même fichier Figma dans une même page nommé <em>Accueil - Version 1</em>: <br>- les 3 frames de la maquette d'accueil (mobile, talbette et desktop) <br>- le frame du moodboard<br>- le frame du guide de styles.
Dans le même fichier Figma, créez une page nommée <em>Accueil - Version 2</em>.
Dans la page <em>Accueil - Version 2</em>, recréez un nouveau moodboard à partir des moodboards présentés par votre équipe en ne gardant que les éléments pertinents suite au commentaires clients. <br><br>Par exemple, si vous aviez une capture d'écran démontrant un type de menu ayant été rejeté par le client, il faudra éviter d'inclure cette capture dans votre nouvelle itération.
Faites une copie du guide de styles initial dans la page <em>Accueil - Version 2</em>. Ajustez le guide de styles de sorte qu'il respecte les commentaires client. Autrement dit, présenter les bonnes couleurs, polices, etc.
Dans la page <em>Accueil - Version 2</em> faites une copie de la maquette initiale de l'accueil. Appliquez les ajustements à la maquette afin de tenir compte des commentaires client. 
Ajoutez ensuite le moodboard, le guide de styles et la maquette <em>(PDF)</em> dans le répertoire GitHub par cette nouvelle itération. Assurez vous de bien nommer les différents fichiers. On veut pouvoir voir les maquette, guide et moodboard V1 et les v2 aussi. Nommez bien les maquettes accueil avec le nom de la page dedant car il y aura d'autres page à concevoir prochainement. Si vous avez modifié le lien Figma (rassemblé les éléments dans un autre fichier), veuillez aussi changer le lien Figma qui se trouve dans le dossier source. S'il n'y est pas, parlez à votre DevOp, c'est lui qui doit le créer à l'étape 7 des requis git.
</checklist>  
  
<warning>Assurez-vous que vos collègues approuvent vos changements, car vous devrez ensuite réaliser cette maquette&nbsp;en équipe et suivre le guide de style sur l'ensemble du site web.</warning>


