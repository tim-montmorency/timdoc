<grostitre>Remise</grostitre>
<p>Il n'y a pas de remise formelle, le fait de remplacer le guide de style, le moodboard, et la maquette de l'accueil mis-à-jour sur le github fera office de remise. N'oubliez pas de corriger le lien vers le Figma en ligne si le lien a changé.</p>
