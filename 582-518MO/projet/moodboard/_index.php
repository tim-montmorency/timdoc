<checklist>Créez un moodboard sur Figma.
Ajoutez au minimum <strong>6 captures d'écran</strong> permettant d'explorer la direction que vous aimeriez donner à votre projet: Polices, combinaison de couleurs, ambiance, fonctionnalités, etc. <br><br>Attention, validez votre moodboard avec les membres de votre équipe avant sa remise puisqu'il est interdit de présenter deux fois le même site dans une même équipe.
Pour chacune de ces captures d'écran, expliquez brièvement l'élément que vous jugez intéressant.</checklist>
    
    

          


