<grostitre>Requis</grostitre>
<checklist>
  Écoutez le clic sur le bouton&nbsp;<em>"Créer&nbsp;un 😺"</em>.
Chaque clic sur ce bouton doit appeler une classe JS insérant un nouvel élément contenant le texte&nbsp;<em>"🐈"</em>&nbsp;et la classe&nbsp;<code>.chat</code>.
Chaque chat doit avoir une opacité de 100%. Cependant, chaque clic doit réduire son opacité de 10%. Autrement dit, si un chat a été cliqué 3x, il devrait avoir une opacité de&nbsp;70%.
Si un chat atteint une opacité de 0%&nbsp;<em>(ou moins  🤷‍♂️)</em>, le chat doit être retiré de la&nbsp;page.</checklist>





