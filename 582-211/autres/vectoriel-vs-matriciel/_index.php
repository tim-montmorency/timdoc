<grostitre>Vectoriel</grostitre>

<p>Comme leur nom l'indique, les images vectorielles sont constituées de vecteurs. Ces vecteurs peuvent prendre la forme de rectangle, cercle, polygone, etc. Bref, de toutes formes géométriques pouvant être définies avec des coordonnées, des lignes droites ou des courbes de Béziers.</p>

<img src="images/svg-vectorielle.png">

<p>Ces images sont produites à l'aide des logiciels:</p>

<ul>
    <li><a target="_blank" href="https://www.adobe.com/ca_fr/products/illustrator.html">Illustrator</a></li>
    <li><a target="_blank" href="https://www.sketch.com/">Sketch</a></li>
    <li><a target="_blank" href="https://www.figma.com/">Figma</a></li>
    <li>etc.</li>
</ul>

<dots></dots>


<grostitre>Matriciel</grostitre>

<p>À l'opposée les images dites matricielles <em>(jpg, png, gif, webp, etc.)</em> sont constituées d'une matrice de pixels permettant de reproduire une image.</p>

<img src="images/svg-matricelle.png">

<p>Plus une image est en haute définition, plus elle contient de pixels dans un même espace. Cependant, plus une image contient de pixels, plus sont poids augmente.</p>

<img src="images/batman-hd-4k.jpg">

<p>Ces images sont produites à l'aide des logiciels:</p>

<ul>
    <li><a target="_blank" href="https://www.adobe.com/ca/products/photoshop/landpa.html">Photoshop</a></li>
    <li><a target="_blank" href="https://www.gimp.org/">Gimp</a></li>
    <li><a target="_blank" href="https://ms-paint.fr.softonic.com/">Paint</a></li>
    <li>etc.</li>
</ul>

<info>Le nom Pixels est la contraction des abréviations "Pics" <em>(picture)</em> et "el" <em>(elements)</em>.</info>

