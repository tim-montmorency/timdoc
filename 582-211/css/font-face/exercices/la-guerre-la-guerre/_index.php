<checklist>
  Rendez-vous sur le site <a onclick="event.stopPropagation();" target="_blank" href="https://www.dafont.com/fr">dafont.com</a>, téléchargez et importez les polices: <em>Gotham Nights</em>, <em>Secret Agent</em>, <em>Enchanted Land</em> et <em>Dopestyle</em> dans votre projet.
  Attribuez une taille de police de 100px à tous les textes du document par défaut. Cette taille devra parfois être modifiée, mais elle vous donnera un point de départ plus simple.
  Appliquez <em>position: absolute</em> sur toutes les balises qui contiennent un mot.
  Sans jamais modifier le HTML, ajustez les styles de chaque mot afin qu'il se rapproche le plus possible du mot correspondant dans le gabarit en arrière-plan.
  Positionnez chaque mot avec les propriétés <em>top</em> et <em>left</em> en utilisant l'unité <em>px</em>.
  Lorsque chaque mot est correctement ajusté, remplacez l'image de gabarit <em>(template.png)</em>, par l'image d'arrière-plan fournie.
