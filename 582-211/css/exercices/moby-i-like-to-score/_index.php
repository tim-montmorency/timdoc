<grostitre>Requis</grostitre>

<warning>Cet exercice n'est pas un exercice flexbox. Vous pouvez appliquer <br><incode>display:inline-block;</incode> sur les éléments <incode>.circle</incode> pour les placer les uns à côté des autres.</warning>

<checklist>
  L'album doit prendre toute la largeur disponible, jusqu'à un maximum de 500px et avoir une hauteur minimale de 500px.
  L'album doit être centré horizontalement dans la page, avoir un fond blanc ⚪️ et une marge intérieure de 30px de tous les côtés n'affectant pas la dimension totale de l'album.
  Le texte doit utiliser la police Georgia <em>(installée par défaut sur tous les ordinateurs)</em> et être aligné comme sur l'image en référence.
  Les éléments avec la classe <span class="inline-code">.circle</span> doivent devenir des cercles de 80x80px avec une marge horizontale de 5px de chaque côté d'entre eux.<br><br>À ce stade-ci, les cercles ne devraient pas tous être en mesure de s'afficher les uns à côté des autres, du a des marges excédentaires.
  Créez une seule règle CSS utilisant une pseudo-classes afin de sélectionner les 2 cercles les plus à gauche ⬅️ et retirer leur marge de gauche <em>(une seule règle devrait impacter les 2 cercles)</em>.<br><img src="images/moby-circles-left.png">
  Créez une une 2e règle CSS utilisant une pseudo-classe afin de sélectionner les 2 cercles les plus à droite ➡️ et retirez leur marge de droite <em>(une seule règle devrait impacter les 2 cercles)</em>.<br><img src="images/moby-circles-right.png">
  Utiliser les pseudo-classes afin de sélectionner les cercles 1, 7 et 9 et changer leur couleur de fond afin qu'ils soient rouge cerise.
</checklist>

