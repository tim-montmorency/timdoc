<p>Timdoc est un système de documentation en ligne conçu spécifiquement pour la technique d'intégration multimédia du Collège Montmorency. Il permet de créer facilement et rapidement des exercices et notes de cours interactives grâce à une collection de composantes faites sur mesure et prêtes à utiliser</p>

<dots></dots>

<grostitre>Technologies</grostitre>

<ul>
    <li>HTML5, SASS/SCSS, Javascript</li>
    <li>PXPros <em>(pré-processeur PHP)</em></li>
    <li>VueJS 3 <em>(composantes)</em></li>
    <li>Highlight.js <em>(surbrillance du code)</em></li>
    <li>Video.js <em>(lecteur vidéo)</em></li>
    <li>Howler.js <em>(lecteur audio)</em></li>
    <li>FFMPEG <em>(transcode média)</em></li>
    <li>MediaInfoLib <em>(informations média)</em></li>
    <li>AudioWaveForm <em>(ondes audio en image)</em></li>
    <li>JQ <em>(JSON en ligne de commande)</em></li>
</ul>

