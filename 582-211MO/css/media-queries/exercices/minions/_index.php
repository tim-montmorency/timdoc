<grostitre>Fenêtre de 450px et +</grostitre>

<checklist>
  Le fond de la page doit devenir jaune.
  Le <span class="inline-code">.wrapper</span> doit avoir une largeur de 360px.
  Les images des Minions doivent avoir une largeur de 175px, gardant toujours l'affichage de deux Minions par ligne.
</checklist>

<dots></dots>

<grostitre>Fenêtre de 800px et +</grostitre>


<checklist>
Le fond de la page doit devenir gris.
Le <span class="inline-code">.wrapper</span> doit avoir une largeur de 620px.
Les images des Minions doivent avoir une largeur de 150px, affichant maintenant les quatre Minions sur une même ligne.
</checklist>

