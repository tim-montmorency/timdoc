<grostitre>Requis de base</grostitre>

<checklist>
  Par défaut, le fond de la page doit-être bleu 🔵 et les textes <em>(incluant les images)</em> doivent être centrés.
  L'élément <span class="inline-code">.wrapper</span> doit avoir une largeur de 310px et être centré horizontalement dans la page.
  Les images des Minions doivent avoir une largeur de 150px, forçant ainsi l'affichage de deux Minions par ligne.
</checklist>

