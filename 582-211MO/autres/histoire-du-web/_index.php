<p>L'histoire du web remonte aux années 1960, lorsque les premières notions d'un réseau informatique mondial ont été développées. Voici une brève chronologie des événements clés de l'histoire du web :</p>

<youtube src="./videos/histoire-internet.json"></youtube>

<grostitre>1960s</grostitre>
<p style="color:#f4754d">Les prémices du réseau</p>

<p>Dans les années 1960, les bases de l'informatique et des réseaux informatiques ont été jetées. Des chercheurs et des scientifiques ont exploré la possibilité de connecter des ordinateurs entre eux pour partager des informations et des ressources.</p>

<grostitre>1980s</grostitre>
<p style="color:#f4754d">La création d'Internet</p>

<p>Dans les années 1980, le concept d'Internet a commencé à prendre forme. L'ARPA (Advanced Research Projects Agency) du Département de la Défense des États-Unis a développé ARPANET, un réseau qui a permis aux chercheurs de diverses universités et instituts de recherche de communiquer et de partager des données.</p>

<grostitre>1990</grostitre>
<p style="color:#f4754d">L'invention du World Wide Web</p>


<p>En 1990, le chercheur britannique Sir Tim Berners-Lee a inventé le World Wide Web (WWW) au CERN en Suisse. Il a créé le premier navigateur Web, le premier serveur Web et le premier langage de programmation pour le web, le HTML (HyperText Markup Language). Le premier site web a été mis en ligne en 1991.</p>

<youtube src="./videos/naissance-du-web.json"></youtube>

<grostitre>1990's</grostitre>
<p style="color:#f4754d">L'expansion du web</p>

<p>Pendant les années 1990, le web a connu une croissance rapide avec la création de nombreux sites web. Les entreprises ont commencé à créer leur présence en ligne, et le web est devenu une plateforme de communication, de commerce et d'échange d'informations.</p>

<grostitre>Années 2010</grostitre>
<p style="color:#f4754d">L'ère du web mobile</p>

<p>Les années 2010 ont été marquées par l'essor du web mobile avec la prolifération des smartphones et des tablettes. Les sites web et les applications mobiles sont devenus essentiels pour les utilisateurs, et les conceptions réactives ont été adoptées pour s'adapter aux différents appareils.</p>

<grostitre>Aujourd'hui</grostitre>
<p style="color:#f4754d">Le web moderne</p>

<p>De nos jours, le web continue de se développer avec l'avènement de nouvelles technologies telles que l'IA (intelligence artificielle), la réalité virtuelle et augmentée, la blockchain, et bien d'autres. Le web est devenu un outil essentiel pour la communication, l'accès à l'information, le divertissement, le commerce électronique et bien plus encore.</p>

<info>L'histoire du web est marquée par des innovations technologiques, des avancées importantes et une évolution constante qui a transformé la manière dont nous interagissons avec l'information et les services en ligne.</info>

