    Dans la tête de votre fichier index.html, ajouter le titre : MYKD à votre page.
    Et n'oubliez pas de lié le fichier <em>style.css</em> à votre fichier index.html.
    Maintenant, dans le <em>body</em>, ajouter les trois balises principales.
