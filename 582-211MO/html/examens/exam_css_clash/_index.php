</checklist>

<grostitre>La colonne de droite</grostitre>

<checklist>
    Créez une section.
    Dans votre section, ajoutez un titre h3 avec le texte : <em>Bienvenue dans notre Communauté de Jeux Vidéo : Où les Mondes Virtuels Deviennent Réalité!</em>
    Suivi d'un paragraphe : <em>Chez nous, les jeux vidéo ne sont pas seulement un passe-temps, ils sont une passion partagée qui nous unit. Notre communauté est un espace dynamique où les joueurs du monde entier se réunissent pour célébrer leur amour pour les jeux vidéo, quels que soient leurs genres, plateformes ou niveaux d'expérience. Nous sommes bien plus qu'un simple groupe de joueurs, nous sommes une famille virtuelle, unie par notre dévouement à l'univers des jeux vidéo.</em>
    Ajoutez un <em>saut de ligne</em>, suivi d'un <u>paragraphe en caractère gras</u> : <em>Ce qui fait de notre communauté un endroit spécial :</em>
    Maintenant, ajoutez une <em>liste ordonnée</em> séparée par des <em>sauts de ligne</em> après chaque élément de la liste.
    Ajoutez les paragraphes ci-dessus.
    Dans chacun des paragraphe, les termes :<em> Diversité des Jeux :  Tous Niveaux Confondus :  Événements Communautaires :  Actualités et Discussions : et Respect et Tolérance :</em> sont formattés en <u>Texte important</u> <small>(indique que le texte a une importance particulière ou un certain sérieux voire un caractère urgent.)</small>
    Dans le 3e élément de la liste, ajoutez le texte : <em>nouer des amitiés</em> c'est un texte mis en exposant.
    Dans le 5e élément de la liste, ajoutez le texte : <em>respect</em> c'est un texte mis en caractère gras.
    Dans le 6e élément de la liste, ajoutez le texte : <em>plongez dans un univers où le jeu vidéo devient une expérience sociale inoubliable</em>  c'est un <em>span</em> et à l'intérieur de ce texte, <em>expérience sociale</em> est <u>souligné</u>.
    Une fois la liste terminée, ajoutez un paragraphe avec le texte : <em>N'oubliez pas de vous présenter sur notre forum et de partager votre histoire de joueur. Vous êtes sur le point de rejoindre une communauté passionnée qui comprend votre amour pour les jeux vidéo. Bienvenue chez vous !</em>
