<grostitre>Débutons notre page web</grostitre>

<checklist>
    Dans l'en-tête, ajoutez l'image <em>logo.png</em>.
    Suivi d'une <em>navigation</em> (n'oubliez pas les balises de navigation) à l'aide d'une <em>liste non ordonnée </em>qui contiendra 6 balises ancres (a). <small>(N'oubliez pas de mettre le # comme lien href)</small> <em>Accueil, À propos de nous, Tournoi, Pages, Nouvelles et Contact</em>.
    Maintenant, dans la partie principale <small>(le contenu)</small>, ajouter l'image <em>banner.webp</em>.
    Ajoutez un titre h5 avec le texte suivant : <em>Membre de notre équipe</em>.
    Ajoutez un titre h2 avec le texte suivant :<em> Membre actif de notre équipe</em>.
    Ensuite, ajouter un tableau qui aura <em>2 rangées</em>. La première rangée contient <em>4 colonnes</em> avec les images : team01, team02, team03 et team04.
    La 2e rangée contient <em>2 colonnes fusionnées</em>. Les deux premières colonnes fusionnées contiennent le texte : <em>Notre meilleur joueur ➠</em> en h2.
    Les <em>2 dernières colonnes fusionnées</em> contiennent l'image : <em>meilleur.webp</em>
    Par la suite, créer une <em>div</em>.
</checklist>

<grostitre>La colonne de gauche</grostitre>

<checklist>
