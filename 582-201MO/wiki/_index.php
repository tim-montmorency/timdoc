    <h2>Figma</h2>

    <p>Démo: Détourer une photo dans Figma:</p>
    <ol>
        <li>Barrez le calque de la photo.</li>
        <li>À l'aide de la plume (Pen) dans Figma, détourez l'élément de la photo. Pensez à ajouter une bordure sur votre tracé afin de bien voir ce que vous faites.</li>
