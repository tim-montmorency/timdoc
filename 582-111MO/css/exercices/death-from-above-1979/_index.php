<grostitre>Requis</grostitre>

<checklist>
  Le fond de la page doit être un dégradé vertical ↕️ partant de <em>orange</em>, à <em>rose</em> jusqu'à <em>bleu</em>.
  La police d'écriture doit avoir une grosseur de <em>12px</em>.
  La zone de contenu principal doit avoir une largeur de <em>60%</em>, mais ne jamais devenir plus petite que <em>380px</em> et jamais plus grande que <em>600px</em>. La zone de contenu doit-être centré verticalement ↕️ et horizontalement ↔️ dans la page.
  Vous devez afficher <em>5 boutons radio</em> correspondant aux <em>5 albums</em> les plus récents de <em>Death From Above 1979</em>.<br>&nbsp;&nbsp;&nbsp;&nbsp;•&nbsp;<em>Heads Up</em><br>&nbsp;&nbsp;&nbsp;&nbsp;•&nbsp;<em>You're a Woman, I'm a Machine</em><br>&nbsp;&nbsp;&nbsp;&nbsp;•&nbsp;<em>The Physical World</em><br>&nbsp;&nbsp;&nbsp;&nbsp;•&nbsp;<em>Outrage! Is Now</em><br>&nbsp;&nbsp;&nbsp;&nbsp;•&nbsp;<em>Is 4 Lovers</em>
  Chaque pochette doit prendre la <em>largeur entière</em> de la zone de contenu, avoir un format <em>carré</em> et avoir un espace vertical ↕️ de <em>10px</em> avec les boutons radio.
  Une seule pochette doit-être visible à la fois. Cette pochette doit correspondre au bouton radio de l'album sélectionné. Par défaut, la pochette de l'album <em>Heads Up</em> doit-être affiché. Lorsqu'un nouveau bouton radio est sélectionné, un <em>fondu d'une seconde</em> doit s'effectuer entre les deux pochettes 🕘.
</checklist>

