<p class="spacer">Il existe plusieurs formats d'images supportées dans les navigateurs. Concentrons-nous pour l'instant sur les formats les plus répandus.</p>

<dots></dots>


<grostitre>Gif</grostitre>

<p>Le format GIF <em>(Graphics Interchange Format)</em> est un format d'image mis au point en 1987 utilisant une compression sans perte et pouvant être utilisé pour des animations. Il peut utiliser jusqu'à 8 bits par pixel avec un maximum de 256 couleurs parmi des nuances sur 24 bits.</p>

<p>
    ➕ Supporte les animations.<br>
    ➕ Supporte la transparence.<br>
    ➖ Palette de couleurs limitée.<br>
    ➖ Peu performant.
</p>

<clip src="videos/it-s-pronounced-jif-not-gif.mp4"></clip>

<span class="remark">Le discours de 5 mots de Steve Wilhite’s, inventeur du GIF, lors de la remise de son prix honorifique soulignant l’ensemble de son œuvre aux 17e <a target="_blank" href="https://www.webbyawards.com/">Webby Awards</a>.</span>

<warning>Il est maintenant généralement plus performant d’utiliser une vidéo qu’un GIF pour afficher une animation.</warning>

<doclink href="https://fr.wikipedia.org/wiki/Graphics_Interchange_Format">GIF</doclink>

<dots></dots>


<grostitre>JPEG</grostitre>

<p>Le JPEG ou JPG est un format d'image sorti en 1992. Il s'agit actuellement du format de compression avec perte le plus populaire. Il est particulièrement efficace avec les images numériques <em>(photographies)</em>. Cependant, son usage avec les diagrammes ou textes demandant des contours bien définis peut produire des résultats indésirables <em>(flou)</em>.</p>

<p>
    ➕ Bon rendu des photographies.<br>
    ➕ Bonne performance.<br>
    ➖ Perte de qualité.<br>
    ➖ Rends flous les textes et lignes droites.<br>
    ➖ Aucun support pour les animations.<br>
    ➖ Aucune transparence.
</p>

<doclink href="https://fr.wikipedia.org/wiki/JPEG">JPEG</doclink>

<dots></dots>


<grostitre>PNG</grostitre>

<p>Le PNG <em>(Portable Network Graphics)</em> est un format de fichiers sorti en 1996 supportant la compression de données sans perte. Autrement dit, il permet à une image de rester plus nette qu'avec un JPEG. En contre partie, son poids est généralement plus élevé.</p>

<p>
    ➕ Aucune perte de qualité.<br>
    ➕ Garde les textes et lignes droites nettes.<br>
    ➕ Supporte la transparence.<br>
    ➖ Moins bonne performance que les JPEG.<br>
    ➖ Aucun support pour les animations.
</p>

<doclink href="https://fr.wikipedia.org/wiki/Portable_Network_Graphics">PNG</doclink>

<dots></dots>


<grostitre>WebP</grostitre>

<p>Le format WebP est le plus jeune des formats parmi ceux mentionnés. Il fut <a target="_blank" href="https://developers.google.com/speed/webp">développé en 2010 par Google</a> dans le but de réduire la quantité de données circulant sur le web. C'est un format de compression d'image avec ou sans perte, selon la configuration choisie. En moyenne, une image <strong>WebP pèse 30% de moins qu'une image compressée en JPEG</strong> et offre une meilleure qualité d'image.</p>

<ul>
    <li><a target="_blank" href="https://developers.google.com/speed/webp/gallery1">Comparaison avec des JPEG</a></li>
    <li><a target="_blank" href="https://developers.google.com/speed/webp/gallery2">Comparaison avec des PNG</a></li>
</ul>

<p>
    ➕ Contrôle sur la qualité.<br>
    ➕ Garde les textes et lignes droites nettes.<br>
    ➕ Supporte les animations.<br>
    ➕ Supporte la transparence.<br>
    ➖ Non supporté sur Safari avant Big Sur <em>(2020)</em>.<br>
    ➖ Non supporté sur Internet Explorer.<br>
</p>

<doclink href="https://fr.wikipedia.org/wiki/WebP">WebP</doclink>

