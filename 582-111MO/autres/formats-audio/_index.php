<p class="spacer">Il existe plusieurs formats Audio supportées dans les navigateurs.</p>



<grostitre>MP3</grostitre>

<p><em>MP3 (MPEG Audio Layer III)</em> Le format MP3 est l'un des formats audio les plus répandus sur le web. Il offre une bonne qualité sonore tout en ayant une taille de fichier relativement petite, ce qui le rend idéal pour le streaming en ligne et le téléchargement.</p>

<p>
    ➕ Taille de fichier réduite<br>
    ➕ Compatibilité universelle<br>
    ➕ Diffusion en continu<br><br>
    ➖ Perte de qualité<br>
    ➖ Qualité variable<br>
    ➖ Dégradation successive
</p>

<info><p>Le MP3 est un format audio largement utilisé en raison de sa taille de fichier réduite et de sa compatibilité universelle, ce qui le rend pratique pour la diffusion en continu et le stockage de musique.</p> 
<p>Cependant, il présente des compromis en termes de qualité audio par rapport aux formats sans perte. Le choix d'utiliser le MP3 dépend des besoins spécifiques et des préférences en matière de qualité audio.</p></info>

<doclink href="https://en.wikipedia.org/wiki/MP3">MP3</doclink>

