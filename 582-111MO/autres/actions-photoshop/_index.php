<p class="spacer">Il est parfois nécessaire d'effectuer un traitement identiques sur plusieurs images 🖼️. Par exemple, les redimensionner ou encore les optimiser afin de diminuer leur poids. Heureusement, il est possible de créer un script appelé <em>action</em> via Photoshop et de lui indiquer un groupe de fichiers sur lesquels s'appliquer.</p>

<dots></dots>


<grostitre>Création</grostitre>

<p>Pour ce faire, dans Photoshop, il faut allez dans l'onglet <em>Fenêtre</em> et sélectionner l'option <em>Actions</em>. La fenêtre d'actions suivante devrait s'afficher.</p>

<img src="images/actions-photoshop-window.jpg">

<p>Il est possible de créer des actions en cliquant sur l'icône +, de les supprimer avec la poubelle 🗑️ ou de les organiser/grouper avec le dossier 📁.</p>

<img src="images/actions-photoshop-create.jpg">

<p>Lorsqu'une action est créée, il faut lui attribuer:</p>

<ul>
    <li>Un nom <em>(Action 1)</em></li>
    <li>Un dossier <em>(Dossier par défaut)</em><br>Il est aussi possible, mais optionnel, de lui attribuer</li>
    <li>Un raccourcis clavier</li>
    <li>Une couleur</li>
</ul>

<dots></dots>


<grostitre>Enregistrement</grostitre>

<p>Il est possible de démarrer l'enregistrement en cliquant sur l'icône 🔴. Dès lors, toutes les manipulations effectuées sur le fichier de référence ouvert seront sauvegardés jusqu'à ce que l'enregistrement soit arrêté.</p>

<p>À tout moment, il est possible d'arrêter l'enregistrement via le bouton ⏹️</p>

<p>Si désiré, une action peut être déclenchée sur le fichier ouvert via le bouton ▶️</p>

<dots></dots>


<grostitre>Appliquer aux fichiers</grostitre>

<p>Une fois l'action enregistrée, il faut l'appliquer aux fichiers désirés. Pour ce faire, il faut allez dans l'onglet <em>Fichier</em>, sélectionner l'option <em>Automatisation</em> et choisir <em>Traitement par lots…</em>. La fenêtre suivante devrait alors s'afficher.</p>

<img src="images/actions-photoshop-batch.jpg">

<p>Il faut maintenant indiquer dans quel dossier se trouve l'action <em>(Ensemble)</em>. Spécifier le nom de l'action désirée <em>(Action)</em>. Indiquer où se trouve les fichiers devant être traités <em>(Source)</em> et optionnellement, donner un dossier de destinations où la nouvelle versions des fichiers pourra être sauvegardée <em>(Destination)</em>.</p>

