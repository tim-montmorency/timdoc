<grostitre>Virtual Studio Code  (VS code)</grostitre>

<img src="images/vs-code.webp">

<p>Visual Studio Code, souvent abrégé en VS Code, est un éditeur de code source développé par Microsoft. Il est conçu pour être léger, rapide et extrêmement personnalisable, ce qui en fait un choix populaire parmi les développeurs pour écrire du code dans une variété de langages de programmation.</p>
<p> VS Code est gratuit et open source, ce qui signifie que les utilisateurs peuvent l'utiliser, le personnaliser et contribuer à son développement.</p>

<doclink href="https://code.visualstudio.com">VS Code</doclink>

