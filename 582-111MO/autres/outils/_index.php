<grostitre>Codepen</grostitre>

<img src="images/codepen.png">

<p>CodePen est une plateforme en ligne populaire qui permet aux développeurs web de créer, expérimenter et partager du code HTML, CSS et JavaScript en temps réel. C'est un environnement de développement web interactif qui offre un espace de travail virtuel pour tester des idées, créer des maquettes, résoudre des problèmes et collaborer avec d'autres développeurs.</p>

<p> CodePen est souvent utilisé pour la création de prototypes, la démonstration de concepts, l'apprentissage et la création de petites applications web.</p>

