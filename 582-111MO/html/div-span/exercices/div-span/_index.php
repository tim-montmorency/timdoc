<grostitre>Instructions</grostitre>

<p>Considérez le paragraphe de texte suivant et suivez les instructions pour appliquer les balises <incode>&lt;div&gt;</incode> et <incode>&lt;span&gt;</incode> de manière appropriée.</p>

<grostitre>Texte</grostitre>

<p>La technologie a évolué à un rythme rapide au cours des dernières décennies. Les ordinateurs sont devenus plus petits et plus puissants, et Internet a transformé la façon dont nous interagissons avec l'information. Aujourd'hui, nous portons des smartphones qui sont essentiellement des ordinateurs miniatures dans nos poches.</p>

<p>La balise <incode>&lt;div&gt;</incode> est un élément essentiel dans la création de mises en page modernes. Elle agit comme un conteneur générique permettant de regrouper des éléments connexes. Par exemple, vous pouvez utiliser une balise <incode>&lt;div&gt;</incode> pour créer un en-tête qui contient le logo et le titre de votre site.</p>

<p>D'un autre côté, la balise <incode>&lt;span&gt;</incode> est utilisée pour appliquer des styles à des parties spécifiques d'un texte. Si vous avez un paragraphe et que vous voulez mettre en évidence un mot ou une phrase, vous pouvez entourer ce contenu avec une balise <incode>&lt;span&gt;</incode> et lui attribuer une classe pour le styliser avec CSS.</p>

<p>En résumé, la balise <incode>&lt;div&gt;</incode> est utilisée pour regrouper des éléments, tandis que la balise <incode>&lt;span&gt;</incode> est utilisée pour appliquer des styles à des parties de texte.
</p>

<grostitre>Tâches</grostitre>

<checklist>
