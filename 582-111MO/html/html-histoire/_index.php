<grostitre>HTML</grostitre>

<h3> Signifie <em>"HyperText Markup Language"</em></h3>

<p>C’est un langage qui permet de composer des pages web.</p>

<p>Il représente en fait une des trois technologies de base que tout développeur web doit connaître car elle permet définir la structure et le contenu de vos pages web.</p>

<p>On parle de <incode style="color:chartreuse">langage de balisage</incode> et non de langage de programmation, car le but du HTML est <incode style="color:chartreuse">d’encadrer</incode> les différents éléments présents dans une page (images, titres, paragraphes...) par des <incode style="color:chartreuse">balises</incode> pour leur <em>donner du sens</em> et aussi leur permettre d’être <incode style="color:chartreuse">mis en forme</incode> via une feuille de style.</p>

<dots></dots>

<grostitre style="text-transform:uppercase; color:gold">Histoire du html</grostitre>

<h4>L'origine du HTML remonte au début du Web.</h4>

<p>En effet, il a été inventé vers les années 1990 afin qu'il puisse présenter les documents qui circulent sur la toile et établir des liens entre eux à travers les liens hypertextes (ou hyperliens).</p>

