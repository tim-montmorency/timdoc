<p style="color:#f4754d">Qu’est-ce que COL.NET au juste?</p>

<p>Col.Net est le portail utilisé par les étudiants et les enseignants du collège. On y retrouve l’horaire, les résultats en cours de session, le QAQ bulletin, les messages de l’administration et tout ce qui vous servira pendant toute la durée de vos études. Prenez l’habitude de vous y connecter souvent!
</p>

