<p style="color:#f4754d">Procédure de connexion au système Col.NET </p>

<img src="images//connexion.png">

<p><em>Code d’usager ÉTUDIANT </em></p>

<p>Il s'agit du numéro de demande d'admission (DA) à 9 caractères. Le numéro de DA apparaît sur la carte d'identité, la lettre d'admission ou la confirmation d'inscription. </p>

<p>Lorsque le numéro de DA est à 7 caractères, il doit être précédé de : </p>

<list>

    <li>19 si le numéro se situe entre 6000000 et 9999999, ainsi 8912034 devient 198912034</li><br>

    <li>20 si le numéro se situe entre 0000000 et 5999999, ainsi 0812034 devient 200812034 </li><br>

</list>

<p><em>Mot de passe </em></p>

<p>Dans tous les cas, lors de la première connexion, la date de naissance est requise sous la forme AAAAMMJJ (exemple : 19510923 pour le 23 septembre 1951). Lors de la première identification, Col.NET demande de créer un mot de passe qui sera utilisé lors des connexions subséquentes. </p>

<p>Vous devez : entrer votre ancien mot de passe (votre date de naissance sous la forme AAAA/MM/JJ) entrer votre nouveau mot de passe (entre 6 et 30 caractères, avec au moins une lettre minuscule, une lettre majuscule et un chiffre) et ensuite le saisir à nouveau pour le confirmer:</p>

<doclink href="https://www.cmontmorency.qc.ca/ColNET/etudiant/Messagesgeneraux/ColNET_procedure_connexion.pdf">Procédure de connexion</doclink>

